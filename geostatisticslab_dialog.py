    def run_qqplot_normality_check(self):
        """Draw side-by-side Q-Q plots for the originally selected field
        (mFieldComboBox) and its NS-transformed counterpart (chosen in
        comboBox_qqplot_ns_field), and report Shapiro-Wilk and
        Anderson-Darling normality test results for both in
        plainTextEdit_normality_test_result."""
        from scipy import stats as scipy_stats

        layer = self.mMapLayerComboBox.currentLayer()
        if layer is None:
            QtWidgets.QMessageBox.warning(self, "No Layer", "Please select a vector layer.")
            return

        field_orig = self.mFieldComboBox.currentField()
        field_ns = self.comboBox_qqplot_ns_field.currentText().strip()
        if not field_orig:
            QtWidgets.QMessageBox.warning(self, "No Field", "Please select a field in the variable combo.")
            return
        if not field_ns:
            QtWidgets.QMessageBox.warning(self, "No NS Field",
                "Please select the NS-transformed field to compare against.")
            return

        def collect_values(fname):
            vals = []
            for feat in layer.getFeatures():
                v = feat[fname]
                try:
                    if v is not None and v == v:  # skip None / NaN
                        vals.append(float(v))
                except (TypeError, ValueError):
                    continue
            return np.array(vals)

        vals_orig = collect_values(field_orig)
        vals_ns = collect_values(field_ns)

        if len(vals_orig) < 8 or len(vals_ns) < 8:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Not enough valid values (need at least 8) to run a "
                "meaningful normality check.")
            return

        # --- Run normality tests on both variables -----------------------------
        def run_tests(vals):
            # Shapiro-Wilk: most reliable for small/medium samples; scipy
            # caps reliable behaviour around a few thousand points, which
            # covers typical geostatistical sample sizes.
            try:
                sw_stat, sw_p = scipy_stats.shapiro(vals)
            except Exception:
                sw_stat, sw_p = np.nan, np.nan

            # Anderson-Darling: gives critical values at fixed significance
            # levels rather than a single p-value.
            try:
                ad_result = scipy_stats.anderson(vals, dist='norm')
                ad_stat = ad_result.statistic
                # 5% significance level critical value (index 2 in scipy's
                # standard significance_level list: 15, 10, 5, 2.5, 1 %)
                ad_crit_5pct = ad_result.critical_values[2]
                ad_reject_5pct = ad_stat > ad_crit_5pct
            except Exception:
                ad_stat, ad_crit_5pct, ad_reject_5pct = np.nan, np.nan, None

            return sw_stat, sw_p, ad_stat, ad_crit_5pct, ad_reject_5pct

        sw_stat_o, sw_p_o, ad_stat_o, ad_crit_o, ad_reject_o = run_tests(vals_orig)
        sw_stat_n, sw_p_n, ad_stat_n, ad_crit_n, ad_reject_n = run_tests(vals_ns)

        # --- Draw the two Q-Q plots side by side --------------------------------
        self.figure_qqplot.clear()
        ax1 = self.figure_qqplot.add_subplot(121)
        ax2 = self.figure_qqplot.add_subplot(122)

        for ax, vals, title in [(ax1, vals_orig, field_orig), (ax2, vals_ns, field_ns)]:
            (osm, osr), (slope, intercept, r) = scipy_stats.probplot(
                vals, dist='norm', fit=True
            )
            ax.scatter(osm, osr, s=14, color='#1f77b4', alpha=0.7, zorder=3)
            line_x = np.array([osm.min(), osm.max()])
            ax.plot(line_x, slope * line_x + intercept, color='red',
                   linewidth=1.3, linestyle='--', zorder=2)
            ax.set_title(title, fontsize=9)
            ax.set_xlabel('Theoretical quantiles', fontsize=8)
            ax.set_ylabel('Sample quantiles', fontsize=8)
            ax.tick_params(axis='both', labelsize=7)
            ax.grid(True, linestyle=':', linewidth=0.4, alpha=0.5)

        self.figure_qqplot.tight_layout()
        self.canvas_qqplot.draw()

        # --- Compose the text summary --------------------------------------------
        def format_block(name, n, sw_stat, sw_p, ad_stat, ad_crit, ad_reject):
            sw_verdict = ("compatible with normality" if (sw_p is not None and not np.isnan(sw_p) and sw_p > 0.05)
                         else "deviates from normality" if not np.isnan(sw_p) else "n/a")
            if ad_reject is None:
                ad_verdict = "n/a"
            else:
                ad_verdict = "deviates from normality" if ad_reject else "compatible with normality"
            return (
                "%s (n=%d):\n"
                "  Shapiro-Wilk: W=%.4f, p=%.4g -> %s\n"
                "  Anderson-Darling: A2=%.4f, crit(5%%)=%.4f -> %s"
                % (name, n, sw_stat, sw_p, sw_verdict, ad_stat, ad_crit, ad_verdict)
            )

        summary = (
            format_block(field_orig, len(vals_orig), sw_stat_o, sw_p_o, ad_stat_o, ad_crit_o, ad_reject_o)
            + "\n\n" +
            format_block(field_ns, len(vals_ns), sw_stat_n, sw_p_n, ad_stat_n, ad_crit_n, ad_reject_n)
        )
        self.plainTextEdit_normality_test_result.setPlainText(summary)

