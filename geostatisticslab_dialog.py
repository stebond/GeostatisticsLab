    def _cross_val_select_previous(self):
        """Select the previous point in the cross-validation results (wraps at 0)."""
        if not getattr(self, '_cross_val_results', None):
            return
        n = len(self._cross_val_results)
        idx = self._cross_val_current_idx - 1
        if idx < 0:
            idx = n - 1  # wrap to last
        self._show_cross_val_point(idx)

    def _cross_val_select_next(self):
        """Select the next point in the cross-validation results (wraps at end)."""
        if not getattr(self, '_cross_val_results', None):
            return
        n = len(self._cross_val_results)
        idx = self._cross_val_current_idx + 1
        if idx >= n:
            idx = 0  # wrap to first
        self._show_cross_val_point(idx)

    def _on_cross_val_table_clicked(self, row, column):
        """Allow the user to select a point directly by clicking its row."""
        if not getattr(self, '_cross_val_results', None):
            return
        self._show_cross_val_point(row)

    def _show_cross_val_point(self, idx):
        """Refresh the weights table and the neighbor map for the point at `idx`
        in the cross-validation results."""
        if not getattr(self, '_cross_val_results', None):
            return
        n = len(self._cross_val_results)
        idx = max(0, min(idx, n - 1))
        self._cross_val_current_idx = idx

        self._populate_cross_kriging_weights_table(idx)
        self._plot_cross_validation_neighbors(idx)

    def _populate_cross_kriging_weights_table(self, idx):
        """Fill tableWidget_cross_kriging_weights with ALL input points (one row
        each): X, Y, <field_name>, then one weight column per neighbor slot up
        to spinBox_kriging_num_points_max.

        Row colouring matches tableView_cross_kriging: light green when
        Error2/Variance <= 1 (good estimate), light red when > 1, no colour
        if the point could not be estimated.

        For the row corresponding to the currently selected point (`idx`):
          - shown in bold to mark it as selected (colour is reserved for
            estimate quality, so selection uses font weight instead)
          - its own weight columns show the weights used to estimate it
            (one value per neighbor actually used, aligned to W1..Wn in the
            order returned by the solver)
        For all other rows, the weight columns are left blank, since those
        weights only have meaning relative to the point being estimated.
        """
        tw = self.tableWidget_cross_kriging_weights
        results = self._cross_val_results
        details_selected = self._cross_val_details[idx]
        field_name = getattr(self, '_cross_val_field_name', 'Value')
        max_weights = self.spinBox_kriging_num_points_max.value()
        NO_VALUE = getattr(self, '_cross_val_NO_VALUE', -9999.0)

        headers = ['X', 'Y', field_name] + \
                 ['W%d' % (i + 1) for i in range(max_weights)]

        tw.setRowCount(len(results))
        tw.setColumnCount(len(headers))
        tw.setHorizontalHeaderLabels(headers)
        tw.verticalHeader().setVisible(False)
        tw.setEditTriggers(QtWidgets.QAbstractItemView.NoEditTriggers)
        tw.setSelectionBehavior(QtWidgets.QAbstractItemView.SelectRows)

        # Same palette as tableView_cross_kriging
        color_green = QtGui.QColor(198, 239, 206)  # light green: ratio <= 1
        color_red   = QtGui.QColor(255, 199, 206)  # light red:   ratio >  1

        def make_item(text, row_color=None, is_selected_row=False):
            item = QtWidgets.QTableWidgetItem(text)
            item.setTextAlignment(QtCore.Qt.AlignCenter)
            item.setFlags(QtCore.Qt.ItemIsEnabled | QtCore.Qt.ItemIsSelectable)
            if row_color is not None:
                item.setBackground(QtGui.QBrush(row_color))
            if is_selected_row:
                font = item.font()
                font.setBold(True)
                item.setFont(font)
            return item

        for row, result in enumerate(results):
            is_sel = (row == idx)

            ratio_val = result['ratio']
            if ratio_val != NO_VALUE:
                row_color = color_green if ratio_val <= 1.0 else color_red
            else:
                row_color = None

            tw.setItem(row, 0, make_item("%.4f" % result['x'], row_color, is_sel))
            tw.setItem(row, 1, make_item("%.4f" % result['y'], row_color, is_sel))
            tw.setItem(row, 2, make_item("%.4f" % result['true'], row_color, is_sel))

            if is_sel and details_selected is not None:
                weights = details_selected['weights']
                for w_col in range(max_weights):
                    col = 3 + w_col
                    text = "%.4f" % weights[w_col] if w_col < len(weights) else ""
                    tw.setItem(row, col, make_item(text, row_color, is_sel))
            else:
                for w_col in range(max_weights):
                    tw.setItem(row, 3 + w_col, make_item("", row_color, is_sel))

        tw.resizeColumnsToContents()
        tw.selectRow(idx)
        tw.scrollToItem(tw.item(idx, 0), QtWidgets.QAbstractItemView.PositionAtCenter)

        tw.resizeColumnsToContents()

    def _plot_cross_validation_neighbors(self, idx):
        """Draw the neighbor map for the selected cross-validation point on
        canvas_cross_neighb.

        - Selected point   : filled red circle
        - Used neighbors    : black edge, fill colour proportional to weight
        - Candidate-but-unused points (inside ellipse, excluded by filters)
          and all other points: grey edge, white fill
        - Search ellipse centred on the selected point, drawn from
          doubleSpinBox_searchR_max / _min / search_azimuth
        - Equal aspect ratio so the ellipse and point layout are not
          distorted (x scale == y scale)
        """
        result  = self._cross_val_results[idx]
        details = self._cross_val_details[idx]
        X_all   = self._cross_val_X
        Y_all   = self._cross_val_Y

        x_sel, y_sel = result['x'], result['y']

        self.figure_cross_neighb.clear()
        ax = self.figure_cross_neighb.add_subplot(111)

        n_all = len(X_all)
        all_idx = np.arange(n_all)

        if details is not None:
            used_idx = set(details['neighbor_idx'].tolist())
        else:
            used_idx = set()

        # Identify the selected point's own index, then everything else
        # that is neither the selected point nor a used neighbor
        sel_point_idx = int(np.argmin((X_all - x_sel)**2 + (Y_all - y_sel)**2))
        other_idx = [i for i in all_idx if i != sel_point_idx and i not in used_idx]

        # --- Unused / non-candidate points: grey edge, white fill -----------
        if other_idx:
            ax.scatter(X_all[other_idx], Y_all[other_idx],
                      facecolors='white', edgecolors='#999999',
                      linewidths=1.2, s=50, zorder=2, label='Not used')

        # --- Used neighbors: black edge, fill proportional to weight --------
        if used_idx:
            n_idx = details['neighbor_idx']
            n_x   = details['neighbor_x']
            n_y   = details['neighbor_y']
            w     = details['weights']
            cmap  = plt.get_cmap('viridis')
            w_min, w_max = w.min(), w.max()
            if w_max == w_min:
                norm_w = np.full_like(w, 0.5)
            else:
                norm_w = (w - w_min) / (w_max - w_min)
            colors = cmap(norm_w)
            sc = ax.scatter(n_x, n_y, c=colors, edgecolors='black',
                           linewidths=1.3, s=80, zorder=3, label='Neighbors (weight)')
            sm = plt.cm.ScalarMappable(cmap=cmap,
                                       norm=plt.Normalize(vmin=w_min, vmax=w_max))
            sm.set_array([])
            cbar = self.figure_cross_neighb.colorbar(sm, ax=ax, fraction=0.035, pad=0.04)
            cbar.set_label('Kriging weight', fontsize=8)
            cbar.ax.tick_params(labelsize=7)

        # --- Selected point: filled red circle ------------------------------
        ax.scatter([x_sel], [y_sel], facecolors='#e41a1c', edgecolors='black',
                  linewidths=1.3, s=110, zorder=4, label='Selected point')

        # --- Search ellipse -----------------------------------------------------
        r_max = self.doubleSpinBox_searchR_max.value()
        r_min = self.doubleSpinBox_searchR_min.value()
        if r_min <= 0:
            r_min = r_max
        azimuth = self.doubleSpinBox_search_azimuth.value()

        theta = np.linspace(0, 2 * np.pi, 200)
        ellipse_x_local = r_max * np.cos(theta)
        ellipse_y_local = r_min * np.sin(theta)
        az_rad = np.radians(azimuth)
        # Rotate from local ellipse frame to map frame (inverse of the
        # rotation used in _estimate_single_point's search-ellipse test)
        ellipse_x = x_sel + ellipse_x_local * np.cos(az_rad) - ellipse_y_local * np.sin(az_rad)
        ellipse_y = y_sel + ellipse_x_local * np.sin(az_rad) + ellipse_y_local * np.cos(az_rad)
        ax.plot(ellipse_x, ellipse_y, color='#1f77b4', linewidth=1.4,
               linestyle='--', zorder=1, label='Search ellipse')

        # --- Equal axis scale: never stretched in either direction ----------
        ax.set_aspect('equal', adjustable='datalim')

        ax.set_xlabel('X', fontsize=8)
        ax.set_ylabel('Y', fontsize=8)
        ax.set_title('Neighbors for point %d/%d' %
                     (idx + 1, len(self._cross_val_results)), fontsize=10)
        ax.grid(True, linestyle=':', linewidth=0.4, alpha=0.5)
        ax.tick_params(axis='both', labelsize=7)
        # Place legend outside the axes, further right than the colorbar
        # (which sits immediately right of the axes), so the two never
        # overlap regardless of point layout.
        legend_x = 1.32 if used_idx else 1.02
        ax.legend(fontsize=7, loc='center left', bbox_to_anchor=(legend_x, 0.5),
                 framealpha=0.9, borderaxespad=0.0)

        # tight_layout() does not reliably reserve space for artists placed
        # outside the axes via bbox_to_anchor; reserve the right margin
        # manually instead so the legend/colorbar are never clipped.
        right_margin = 0.62 if used_idx else 0.78
        self.figure_cross_neighb.subplots_adjust(left=0.12, right=right_margin,
                                                 top=0.90, bottom=0.12)
        self.canvas_cross_neighb.draw()

    def prompt_save_path(self):
            """Open a file-save dialog and return the chosen output path."""
            layer = self.mMapLayerComboBox.currentLayer()
            field_name = self.mFieldComboBox.currentField()
            
            if not layer or not field_name:
                QtWidgets.QMessageBox.warning(self, "Attenzione", "Seleziona un layer e una variabile validi.")
                return None
    
            # Suggested output file name
            # Set up the kriging type
            is_uk = self.radioButton_UniversalKriging.isChecked()
            is_sk = self.radioButton_SimpleKriging.isChecked()
            is_ok = self.radioButton_OrdinaryKriging.isChecked()
            if is_ok:
                 string_name_type="ok"
            elif is_uk:
                 string_name_type="uk"
            else:
                string_name_type="sk"
            suggested_name = f"{field_name}_{string_name_type}.tif"
            
            # Open the save-file dialog
            path, _ = QtWidgets.QFileDialog.getSaveFileName(
                self, "Save Estimated Raster", suggested_name, "GeoTIFF (*.tif)"
            )
            
            if path:
                # Check existence both on disk and inside the QGIS project
                final_path = self.get_unique_filename(path)
                self.lineEdit_estimated_layer_name.setText(final_path)
                return final_path
            return None
    def get_unique_filename(self, path):
            """Controlla se il file esiste su disco o nei layer di QGIS e aggiunge un indice."""
            base, ext = os.path.splitext(path)
            counter = 1
            current_path = path
            
            # Check if the output file already exists on disk
            # Check if a layer with this name already exists in the QGIS project
            while os.path.exists(current_path) or QgsProject.instance().mapLayersByName(os.path.basename(current_path)):
                current_path = f"{base}_{counter}{ext}"
                counter += 1
            return current_path
