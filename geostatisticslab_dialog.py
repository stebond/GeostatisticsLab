    @staticmethod
    def _autovar_adaptive_lag(coords, n_neighbors=4, min_spacing=1.0):
        """Replicate the lag-spacing algorithm from the AutoVar Fortran
        project (subroutine datspac_horz in datspac_xy.f90):

        For each point, find its n_neighbors nearest *distinct* neighbors
        (duplicates closer than min_spacing apart are skipped, to avoid
        near-zero distances from coincident locations), and record the
        minimum, average, and maximum distance to those neighbors. Then
        average each of those three quantities across all points, giving
        three domain-wide spacing statistics:

            avg_min : average of each point's nearest-neighbor distance
            avg_avg : average of each point's mean distance to its
                      n_neighbors nearest neighbors (used as the
                      "default" lag size, matching AutoVar's primary
                      direction lag)
            avg_max : average of each point's farthest distance among its
                      n_neighbors nearest neighbors

        Returns (avg_min, avg_avg, avg_max).
        """
        n = len(coords)
        if n < 2:
            return 0.0, 0.0, 0.0

        k = min(n_neighbors, n - 1)
        dist_min = np.zeros(n)
        dist_avg = np.zeros(n)
        dist_max = np.zeros(n)

        for i in range(n):
            dx = coords[:, 0] - coords[i, 0]
            dy = coords[:, 1] - coords[i, 1]
            d = np.sqrt(dx**2 + dy**2)
            d = d[d >= min_spacing]  # drop self (d=0) and near-duplicate points
            if len(d) == 0:
                continue
            d_sorted = np.sort(d)[:k]
            dist_min[i] = d_sorted[0]
            dist_max[i] = d_sorted[-1]
            dist_avg[i] = np.mean(d_sorted)

        valid = dist_avg > 0
        if not np.any(valid):
            return 0.0, 0.0, 0.0

        avg_min = float(np.mean(dist_min[valid]))
        avg_avg = float(np.mean(dist_avg[valid]))
        avg_max = float(np.mean(dist_max[valid]))
        return avg_min, avg_avg, avg_max

    def calculate_variogram(self):
        layer = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name: return

        max_dist = self.doubleSpinBox_max_distance.value()
        num_lags = self.spinBox_num_lags.value()
        lag_size = self.doubleSpinBox_lag.value()  
        lag_tol = self.doubleSpinBox_lag_tolerance.value()
        num_dirs = self.spinBox_num_directions.value()
        ang_tol = self.doubleSpinBox_angular_tolerance.value()
        bandwidth = self.doubleSpinBox_bandwidth.value()
        base_azimuth = self.doubleSpinBox_azimuth.value()

        coords, values = [], []
        for f in layer.getFeatures():
            if f.hasGeometry():
                val = f[field_name]
                if val != NULL and val is not None:
                    try:
                        pt = f.geometry().asPoint()
                        coords.append([pt.x(), pt.y()])
                        values.append(float(val))
                    except (ValueError, TypeError): continue

        coords, values = np.array(coords), np.array(values)
        num_points = len(values)
        if num_points < 2: return
        sample_variance = float(np.var(values))

        # -- Optional declustering weights ----------------------------------------
        # If the user enabled checkBox_use_decluster_weights and the layer has
        # a 'decluster_weight' field (written by run_declustering), each pair
        # (i, j) is weighted by w_i * w_j -- the standard GSLIB convention --
        # instead of every pair counting equally.
        use_decluster = (hasattr(self, 'checkBox_use_decluster_weights')
                         and self.checkBox_use_decluster_weights.isChecked()
                         and "decluster_weight" in {f.name() for f in layer.fields()})
        if use_decluster:
            point_weights = []
            for f in layer.getFeatures():
                if f.hasGeometry():
                    val = f[field_name]
                    if val != NULL and val is not None:
                        try:
                            float(val)
                            w = f["decluster_weight"]
                            point_weights.append(float(w) if w not in (None, NULL) else 1.0)
                        except (ValueError, TypeError):
                            continue
            point_weights = np.array(point_weights)
            if len(point_weights) != num_points:
                # Safety fallback: if collection got out of sync for any
                # reason, do not silently apply mismatched weights.
                use_decluster = False
                point_weights = np.ones(num_points)
        else:
            point_weights = np.ones(num_points)

        # -- AutoVar-style adaptive lag spacing -----------------------------------
        # When enabled, the lag size, tolerance, and number of lags are
        # derived from the actual nearest-neighbor spacing of the data
        # (replicating the datspac_horz algorithm from the AutoVar Fortran
        # project) instead of being read as fixed values from the spinboxes.
        if hasattr(self, 'radioButton_adaptive_lag') and self.radioButton_adaptive_lag.isChecked():
            avg_min, avg_avg, avg_max = self._autovar_adaptive_lag(coords)
            if avg_avg > 0:
                lag_size = avg_avg
                lag_tol  = lag_size / 2.0
                num_lags = max(1, int(max_dist / lag_size) + 1)
                # Reflect the computed values back into the spinboxes so the
                # user can see exactly what was used (and so the variogram
                # map / other tools that read these spinboxes stay in sync).
                self.doubleSpinBox_lag.blockSignals(True)
                self.doubleSpinBox_lag_tolerance.blockSignals(True)
                self.spinBox_num_lags.blockSignals(True)
                self.doubleSpinBox_lag.setValue(lag_size)
                self.doubleSpinBox_lag_tolerance.setValue(lag_tol)
                self.spinBox_num_lags.setValue(num_lags)
                self.doubleSpinBox_lag.blockSignals(False)
                self.doubleSpinBox_lag_tolerance.blockSignals(False)
                self.spinBox_num_lags.blockSignals(False)

        self.progressBar_vario.setValue(0)
        # GSLIB/AutoVar bin convention: centers at h = 0, lag, 2*lag, ...
        # (NOT lag/2, 3*lag/2, ... as a naive "midpoint of [0,lag]" reading
        # might suggest). Each bin k>=1 has tolerance lag_size/2 on either
        # side of k*lag_size; bin 0 only has the upper half (it cannot
        # extend to negative distances), so its effective tolerance is
        # lag_size/2 on the high side only. With num_lags=1, the only bin
        # is centered at h=0 -- to see a point further out, increase
        # num_lags so a bin centered at h=lag_size is also computed.
        lag_centers = np.arange(num_lags) * lag_size
        # Per-bin tolerance: lag_size/2 everywhere, except bin 0 which uses
        # lag_size/2 as well but is implicitly one-sided (handled by the
        # abs(dist - center) <= lag_tol check below, since dist >= 0 always
        # means the "lower half" of bin 0's nominal window is never reached).
        lag_tol = lag_size / 2.0
        # The tolerance is always derived from lag_size in this convention
        # (matching GSLIB/AutoVar's use of IDNINT for bin assignment, which
        # is mathematically equivalent to a +/- lag/2 window). Reflect that
        # back into the spinbox and disable manual editing, so the value
        # shown is never stale or misleading.
        self.doubleSpinBox_lag_tolerance.blockSignals(True)
        self.doubleSpinBox_lag_tolerance.setValue(lag_tol)
        self.doubleSpinBox_lag_tolerance.blockSignals(False)
        # Disable manual editing only in adaptive mode: lag_tol is always
        # lag/2 and is derived automatically. In fixed mode the user can
        # set any tolerance they like.
        is_adaptive = (hasattr(self, 'radioButton_adaptive_lag') and
                       self.radioButton_adaptive_lag.isChecked())
        self.doubleSpinBox_lag_tolerance.setEnabled(not is_adaptive)
        azimuths = [(base_azimuth + d * (180.0 / num_dirs)) % 180.0 for d in range(num_dirs)]
        ang_tol_rad = np.radians(ang_tol)
        variogram_results = {}

        # -- Optional behaviors -----------------------------------------------
        # checkBox_Normalize_by_variance: divide gamma(h) by the global
        # sample variance, so the sill tends to 1 (relative variogram),
        # useful for comparing variables/datasets on the same scale.
        normalize_by_variance = (hasattr(self, 'checkBox_Normalize_by_variance')
                                 and self.checkBox_Normalize_by_variance.isChecked())

        # use_effective_distance: AutoVar-style behavior -- plot/report each
        # bin at the actual mean distance of the pairs that fell into it,
        # rather than at the bin's theoretical center. This average can
        # legitimately differ from one direction to another, and between
        # bins, depending on how pairs are actually distributed in space.
        use_effective_distance = (hasattr(self, 'checkBox_effective_distance')
                                  and self.checkBox_effective_distance.isChecked())

        total_steps = num_dirs * num_points
        step_count = 0

        for az in azimuths:
            gamma_values = [[] for _ in range(num_lags)]
            pair_weights = [[] for _ in range(num_lags)]
            pair_dists   = [[] for _ in range(num_lags)]
            azimuth_rad = np.radians(90.0 - az)
            u_x, u_y = np.cos(azimuth_rad), np.sin(azimuth_rad)

            for i in range(num_points):
                step_count += 1
                if i % 10 == 0:
                    self.progressBar_vario.setValue(int((step_count / total_steps) * 100))
                    QtWidgets.QApplication.processEvents()

                for j in range(i + 1, num_points):
                    dx, dy = coords[j][0] - coords[i][0], coords[j][1] - coords[i][1]
                    dist = np.sqrt(dx**2 + dy**2)
                    if dist > max_dist or dist == 0: continue

                    cos_alpha = np.clip((dx * u_x + dy * u_y) / dist, -1.0, 1.0)
                    alpha = np.arccos(np.abs(cos_alpha))
                    if alpha > ang_tol_rad: continue
                    if bandwidth > 0 and dist * np.sin(alpha) > bandwidth: continue

                    for k, center in enumerate(lag_centers):
                        if np.abs(dist - center) <= lag_tol:
                            gamma_values[k].append(0.5 * ((values[i] - values[j]) ** 2))
                            # GSLIB convention: pair weight = w_i * w_j. With
                            # no declustering, point_weights are all 1, so
                            # this is equivalent to an unweighted pair.
                            pair_weights[k].append(point_weights[i] * point_weights[j])
                            pair_dists[k].append(dist)

            experimental_gamma = np.zeros(num_lags)
            pair_counts = np.zeros(num_lags)
            # Effective mean distance per bin (AutoVar-style): the actual
            # average separation of the pairs that fell into this lag bin,
            # rather than the bin's theoretical center. With lag=1000 and
            # tolerance=500, a bin nominally spanning 0-1000 (center h=500)
            # may in practice contain pairs averaging closer to, say, 420 or
            # 480, depending on the real spatial distribution -- and this
            # average can differ from one direction to another, since each
            # direction's pairs are not evenly spread across the bin either.
            h_effective = np.copy(lag_centers)
            for k in range(num_lags):
                vals = gamma_values[k]
                wts = pair_weights[k]
                dists = pair_dists[k]
                pair_counts[k] = len(vals)  # raw pair count, used for plotting/coloring
                if len(vals) > 0:
                    wts_arr = np.array(wts)
                    w_sum = np.sum(wts_arr)
                    experimental_gamma[k] = (np.sum(wts_arr * np.array(vals)) / w_sum
                                            if w_sum > 0 else np.mean(vals))
                    if use_effective_distance:
                        h_effective[k] = np.mean(dists)
                else:
                    experimental_gamma[k] = np.nan

            # Optional normalization by the global sample variance (sill =
            # 1 convention), so different variables/datasets can be
            # compared on the same relative vertical scale.
            if normalize_by_variance and sample_variance > 0:
                experimental_gamma = experimental_gamma / sample_variance

            variogram_results[az] = (experimental_gamma, pair_counts, gamma_values, h_effective, pair_dists)

        self.progressBar_vario.setValue(100)
        self.last_lags, self.last_results = lag_centers, variogram_results
        self.last_layer_id, self.last_field_name = layer.id(), field_name
        self.variogram_computed = True # Set flag to true as experimental calculation succeeded
        
        self.plot_multi_variogram(lag_centers, variogram_results)
        self.update_fit_error()

    def calculate_variogram_map(self):
        """Compute a circular variogram map: the experimental semivariance
        binned into (radius ring x angular sector) cells covering the full
        360 degrees around each point pair, then displayed as a polar
        color-coded plot in plotWidgetVariogram_map.

        Unlike the standard directional variogram (symmetric over 180
        degrees), the variogram map distinguishes direction over the full
        circle, so every ordered pair contributes once at the angle from i
        to j (not just the unordered pair at one azimuth class).
        """
        layer = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name:
            return

        max_dist  = self.doubleSpinBox_max_distance_variogram_map.value()
        num_lags  = self.spinBox_num_lags_variogram_map.value()
        lag_size  = self.doubleSpinBox_lag_variogram_map.value()
        lag_tol   = self.doubleSpinBox_lag_tolerance_variogram_map.value()
        num_dirs  = self.spinBox_num_directions_variogram_map.value()
        ang_tol   = self.doubleSpinBox_angular_tolerance_variogram_map.value()
        bandwidth = self.doubleSpinBox_bandwidth_variogram_map.value()
        base_azimuth = self.doubleSpinBox_azimuth_variogram_map.value()

        if num_lags <= 0 or num_dirs <= 0:
            return

        coords, values = [], []
        for f in layer.getFeatures():
            if f.hasGeometry():
                val = f[field_name]
                if val != NULL and val is not None:
                    try:
                        pt = f.geometry().asPoint()
                        coords.append([pt.x(), pt.y()])
                        values.append(float(val))
                    except (ValueError, TypeError):
                        continue

        coords, values = np.array(coords), np.array(values)
        num_points = len(values)
        if num_points < 2:
            return

        # -- Optional declustering weights (separate checkbox from the
        # standard directional variogram, since the two tools are used
        # independently) --------------------------------------------------
        use_decluster = (hasattr(self, 'checkBox_use_decluster_weights_variogram_map')
                         and self.checkBox_use_decluster_weights_variogram_map.isChecked()
                         and "decluster_weight" in {f.name() for f in layer.fields()})
        if use_decluster:
            point_weights = []
            for f in layer.getFeatures():
                if f.hasGeometry():
                    val = f[field_name]
                    if val != NULL and val is not None:
                        try:
                            float(val)
                            w = f["decluster_weight"]
                            point_weights.append(float(w) if w not in (None, NULL) else 1.0)
                        except (ValueError, TypeError):
                            continue
            point_weights = np.array(point_weights)
            if len(point_weights) != num_points:
                use_decluster = False
                point_weights = np.ones(num_points)
        else:
            point_weights = np.ones(num_points)

        self.progressBar_vario_variogram_map.setValue(0)

        # Ring (radius) bin centers following the GSLIB/AutoVar convention:
        # centers at h = 0, lag, 2*lag, ..., (num_lags-1)*lag, so that
        # MaxDistance = (num_lags - 1) * lag_size.
        # The first bin (h = 0) fills the central disc and eliminates the
        # empty circle that would otherwise appear at the centre of the map.
        lag_centers = np.arange(num_lags) * lag_size

        # Sector centers covering the full circle (0..360), offset by the
        # base azimuth, each sector lasting 360/num_dirs degrees
        sector_width = 360.0 / num_dirs
        sector_centers = [(base_azimuth + d * sector_width) % 360.0 for d in range(num_dirs)]

        # Accumulators: rows = lags (rings), columns = sectors (angles)
        gamma_sum    = np.zeros((num_lags, num_dirs))
        weight_sum   = np.zeros((num_lags, num_dirs))
        pair_count   = np.zeros((num_lags, num_dirs))

        total_steps = num_points
        for i in range(num_points):
            if i % 10 == 0:
                self.progressBar_vario_variogram_map.setValue(int((i + 1) / total_steps * 100))
                QtWidgets.QApplication.processEvents()

            # Consider every ordered pair (i, j) with j != i: the direction
            # from i to j spans the full 360 degrees, unlike the standard
            # variogram which only needs the unordered pair (symmetric over
            # 180 degrees).
            dx = coords[:, 0] - coords[i, 0]
            dy = coords[:, 1] - coords[i, 1]
            dist = np.sqrt(dx**2 + dy**2)

            valid = (dist > 0) & (dist <= max_dist)
            valid[i] = False
            if not np.any(valid):
                continue

            dx_v, dy_v, dist_v = dx[valid], dy[valid], dist[valid]
            # Angle of each pair, geographic convention (0 deg = North,
            # clockwise), consistent with the standard variogram's azimuth
            # convention used elsewhere in this file.
            angle_deg = (90.0 - np.degrees(np.arctan2(dy_v, dx_v))) % 360.0
            gamma_pair = 0.5 * (values[i] - values[valid]) ** 2
            # GSLIB convention: pair weight = w_i * w_j. With no
            # declustering, point_weights are all 1, so this is equivalent
            # to an unweighted pair (weight_sum == pair_count numerically).
            weight_pair = point_weights[i] * point_weights[valid]

            for k, r_center in enumerate(lag_centers):
                in_ring = np.abs(dist_v - r_center) <= lag_tol
                if not np.any(in_ring):
                    continue
                for s, a_center in enumerate(sector_centers):
                    # Smallest angular distance on a circle (handles wrap-around at 0/360)
                    diff = np.abs((angle_deg - a_center + 180.0) % 360.0 - 180.0)
                    in_sector = in_ring & (diff <= ang_tol)
                    if bandwidth > 0:
                        # Perpendicular (cross-radial) distance, used to cap
                        # the sector width at large radii, same role as the
                        # bandwidth parameter in the standard variogram.
                        perp_dist = dist_v * np.sin(np.radians(diff))
                        in_sector = in_sector & (np.abs(perp_dist) <= bandwidth)
                    n_in = np.count_nonzero(in_sector)
                    if n_in > 0:
                        gamma_sum[k, s]  += np.sum(gamma_pair[in_sector])
                        weight_sum[k, s] += np.sum(weight_pair[in_sector])
                        pair_count[k, s] += n_in

        self.progressBar_vario_variogram_map.setValue(100)

        with np.errstate(invalid='ignore', divide='ignore'):
            gamma_map = np.where(pair_count > 0, gamma_sum / weight_sum, np.nan)

        self.last_vario_map_data = {
            'lag_centers': lag_centers,
            'sector_centers': np.array(sector_centers),
            'sector_width': sector_width,
            'gamma_map': gamma_map,
            'pair_count': pair_count,
            'lag_size': lag_size,
            'max_dist': max_dist,
            'lag_tol': lag_tol,
        }
        self._plot_variogram_map()
        self._plot_variogram_map_3d()

    def _plot_variogram_map(self):
        """Render the circular variogram map stored in self.last_vario_map_data
        as a polar heat-map: each (ring x sector) cell is colored by its
        semivariance value; cells with zero pairs are left white. The plot
        uses equal X/Y scale so the circle is never stretched."""
        data = getattr(self, 'last_vario_map_data', None)
        if data is None:
            return

        lag_centers    = data['lag_centers']
        sector_centers = data['sector_centers']
        sector_width   = data['sector_width']
        gamma_map      = data['gamma_map']
        pair_count     = data['pair_count']
        lag_size       = data['lag_size']
        max_dist       = data['max_dist']
        lag_tol        = data['lag_tol']

        num_lags = len(lag_centers)
        num_dirs = len(sector_centers)

        # Ring edges: each ring spans +/- lag_size/2 around its center,
        # except the outermost ring, whose outer edge reflects the actual
        # tolerance used when binning pairs into that ring: pairs up to
        # max_dist + lag_tol/2 were included, so the plotted outer radius
        # must extend that far too (not just to max_dist, and not to the
        # inter-center gap, both of which under/over-stated the true extent).
        inner_edge = max(lag_centers[0] - lag_size / 2.0, 0.0)
        outer_edge = max_dist + lag_tol / 2.0
        ring_edges = np.concatenate(([inner_edge], (lag_centers[:-1] + lag_centers[1:]) / 2.0,
                                     [outer_edge]))

        # Sector edges: each sector spans +/- half its width around its center
        half_w = sector_width / 2.0
        sector_edges = np.array([c - half_w for c in sector_centers] + [sector_centers[-1] + half_w])

        self.figure_vario_map.clear()
        ax = self.figure_vario_map.add_subplot(111)

        valid_gamma = gamma_map[np.isfinite(gamma_map)]
        if valid_gamma.size > 0:
            vmin, vmax = float(np.min(valid_gamma)), float(np.max(valid_gamma))
        else:
            vmin, vmax = 0.0, 1.0
        if vmin == vmax:
            vmax = vmin + 1.0
        cmap = plt.get_cmap('viridis')
        norm = plt.Normalize(vmin=vmin, vmax=vmax)

        for k in range(num_lags):
            r_in, r_out = ring_edges[k], ring_edges[k + 1]
            for s in range(num_dirs):
                a_in_deg, a_out_deg = sector_edges[s], sector_edges[s + 1]
                # Convert geographic angle (0=N, clockwise) to math angle
                # (0=East, counter-clockwise) for plotting with matplotlib wedges.
                theta1 = 90.0 - a_out_deg
                theta2 = 90.0 - a_in_deg

                n_pairs = pair_count[k, s]
                if n_pairs <= 0 or not np.isfinite(gamma_map[k, s]):
                    face_color = 'white'
                else:
                    face_color = cmap(norm(gamma_map[k, s]))

                wedge = patches.Wedge((0, 0), r_out, theta1, theta2,
                                      width=(r_out - r_in),
                                      facecolor=face_color,
                                      edgecolor='#888888', linewidth=0.3)
                ax.add_patch(wedge)

        max_r = ring_edges[-1]

        # -- Main anisotropy directions via the inertia tensor ------------------
        if self.checkBox_compute_main_directions_variogram_map.isChecked():
            major_angle_deg, minor_angle_deg = self._compute_variogram_map_main_directions(
                lag_centers, sector_centers, gamma_map, pair_count
            )
            if major_angle_deg is not None:
                self._draw_main_direction_arrows(ax, major_angle_deg, minor_angle_deg, max_r)

        ax.set_xlim(-max_r, max_r)
        ax.set_ylim(-max_r, max_r)
        ax.set_aspect('equal', adjustable='box')  # equal X/Y scale, no stretching

        ax.set_xlabel('X distance', fontsize=8)
        ax.set_ylabel('Y distance', fontsize=8)
        ax.set_title('Variogram Map', fontsize=10)
        ax.grid(True, linestyle=':', linewidth=0.4, alpha=0.5)
        ax.tick_params(axis='both', labelsize=7)

        sm = plt.cm.ScalarMappable(cmap=cmap, norm=norm)
        sm.set_array([])
        cbar = self.figure_vario_map.colorbar(sm, ax=ax, fraction=0.035, pad=0.04)
        cbar.set_label('Semivariance', fontsize=8)
        cbar.ax.tick_params(labelsize=7)

        self.figure_vario_map.tight_layout()
        self.canvas_vario_map.draw()

    def _compute_variogram_map_main_directions(self, lag_centers, sector_centers,
                                               gamma_map, pair_count):
        """Find the principal anisotropy directions of the variogram map using
        a 2D inertia-tensor analysis.

        Each valid cell (ring k, sector s) is treated as a point mass located
        at its (x, y) position on the map, weighted by 1/gamma (cells with
        low semivariance -- i.e. high spatial continuity -- get more weight,
        so the "mass" of the map is concentrated along directions of greater
        continuity). The eigenvectors of the resulting 2x2 inertia tensor
        give the major axis (direction of maximum continuity / minimum
        semivariance growth) and the minor axis (direction of minimum
        continuity), which are exactly perpendicular.

        Returns (major_angle_deg, minor_angle_deg) in the same geographic
        convention used elsewhere (0 deg = North, clockwise), or
        (None, None) if there isn't enough valid data.
        """
        valid = (pair_count > 0) & np.isfinite(gamma_map) & (gamma_map > 0)
        if not np.any(valid):
            return None, None

        # Cell centers in cartesian coordinates (geographic angle -> math angle)
        rr, aa = np.meshgrid(lag_centers, sector_centers, indexing='ij')
        theta_rad = np.radians(90.0 - aa)
        xs = rr * np.cos(theta_rad)
        ys = rr * np.sin(theta_rad)

        # Weight: inverse of semivariance, so low-variance (high continuity)
        # cells dominate the inertia tensor and define the major axis.
        weights = np.zeros_like(gamma_map)
        weights[valid] = 1.0 / gamma_map[valid]

        xs_v, ys_v, w_v = xs[valid], ys[valid], weights[valid]
        w_sum = np.sum(w_v)
        if w_sum <= 0:
            return None, None

        # 2x2 weighted inertia (second-moment) tensor about the origin
        Ixx = np.sum(w_v * xs_v**2) / w_sum
        Iyy = np.sum(w_v * ys_v**2) / w_sum
        Ixy = np.sum(w_v * xs_v * ys_v) / w_sum
        inertia_tensor = np.array([[Ixx, Ixy], [Ixy, Iyy]])

        eigvals, eigvecs = np.linalg.eigh(inertia_tensor)
        # eigh returns eigenvalues in ascending order; the LARGEST eigenvalue's
        # eigenvector points along the direction of greatest spread of mass,
        # i.e. the major (max continuity) axis.
        major_vec = eigvecs[:, np.argmax(eigvals)]
        minor_vec = eigvecs[:, np.argmin(eigvals)]

        def vec_to_geographic_angle(vec):
            # vec = (x, y) in map/cartesian coordinates -> geographic angle
            # (0 deg = North/+Y, clockwise), matching the convention used
            # for sector_centers throughout this feature.
            math_angle = np.degrees(np.arctan2(vec[1], vec[0]))
            return (90.0 - math_angle) % 180.0  # axis, not a vector: fold to 0-180

        major_angle_deg = vec_to_geographic_angle(major_vec)
        minor_angle_deg = vec_to_geographic_angle(minor_vec)

        return major_angle_deg, minor_angle_deg

    def _draw_main_direction_arrows(self, ax, major_angle_deg, minor_angle_deg, max_r):
        """Draw the two principal anisotropy directions as double-headed
        arrows (lines through the origin) on the variogram map axes."""
        def angle_to_xy_unit(angle_deg):
            math_angle_rad = np.radians(90.0 - angle_deg)
            return np.cos(math_angle_rad), np.sin(math_angle_rad)

        arrow_len = max_r * 0.92

        ux, uy = angle_to_xy_unit(major_angle_deg)
        ax.annotate('', xy=(ux * arrow_len, uy * arrow_len),
                   xytext=(-ux * arrow_len, -uy * arrow_len),
                   arrowprops=dict(arrowstyle='<->', color='red', linewidth=2.0),
                   zorder=5)
        ax.text(ux * arrow_len * 1.05, uy * arrow_len * 1.05,
               'Major (%.0f deg)' % major_angle_deg,
               color='red', fontsize=7, fontweight='bold',
               ha='center', va='center', zorder=6)

        vx, vy = angle_to_xy_unit(minor_angle_deg)
        ax.annotate('', xy=(vx * arrow_len, vy * arrow_len),
                   xytext=(-vx * arrow_len, -vy * arrow_len),
                   arrowprops=dict(arrowstyle='<->', color='blue', linewidth=2.0),
                   zorder=5)
        ax.text(vx * arrow_len * 1.05, vy * arrow_len * 1.05,
               'Minor (%.0f deg)' % minor_angle_deg,
               color='blue', fontsize=7, fontweight='bold',
               ha='center', va='center', zorder=6)

    def _plot_variogram_map_3d(self):
        """Render the variogram map as a 3D surface (X, Y, semivariance) in
        plotWidgetVariogram_map_3D. Cells with no pairs are left as gaps
        (NaN) in the surface, matching the 2D map's "blank" convention.

        If checkBox_3D_Variogram_model is checked, the theoretical model
        surface (from the fitted structures in tableWidget_models) is drawn
        on top with transparency, so both surfaces remain visible at once.
        """
        data = getattr(self, 'last_vario_map_data', None)
        if data is None:
            return

        lag_centers    = data['lag_centers']
        sector_centers = data['sector_centers']
        gamma_map      = data['gamma_map']        # shape (num_lags, num_dirs), NaN = no pairs
        pair_count     = data['pair_count']

        # Cell-center cartesian coordinates, same convention as the 2D map
        # (geographic angle, 0 deg = North, clockwise).
        rr, aa = np.meshgrid(lag_centers, sector_centers, indexing='ij')
        rr = np.hstack((rr, rr[:, :1]))
        aa = np.hstack((aa, aa[:, :1] + 360.0))
        theta_rad = np.radians(90.0 - aa)
        xs = rr * np.cos(theta_rad)
        ys = rr * np.sin(theta_rad)

        # Gaps where there are no pairs: NaN values create holes in the
        # plotted surface (matplotlib's plot_surface skips NaN vertices).
        zs_exp = np.where(pair_count > 0, gamma_map, np.nan)
        zs_exp = np.hstack((zs_exp, zs_exp[:, :1]))
        self.figure_vario_map_3d.clear()
        ax = self.figure_vario_map_3d.add_subplot(111, projection='3d')

        show_model = self.checkBox_3D_Variogram_model.isChecked()

        # Determine a shared color scale across both surfaces (experimental
        # and, if shown, theoretical) so colors are comparable.
        finite_vals = [zs_exp[np.isfinite(zs_exp)]]
        zs_model = None
        if show_model:
            structures = self.get_models_from_table()
            if structures:
                rr_model, aa_model = np.meshgrid(
                    lag_centers,
                    sector_centers,
                    indexing='ij'
                )
                
                n_lags = len(lag_centers)
                n_dirs = len(sector_centers)
                h_flat = rr_model.ravel()
                az_flat = aa_model.ravel()

                gamma_flat = self.calculate_theoretical_semivariance(
                    h_flat,
                    az_flat,
                    structures
                )

                zs_model = gamma_flat.reshape((n_lags, n_dirs))
                # chiusura 360 degrees -> 0 degrees
                zs_model = np.hstack((zs_model, zs_model[:, :1]))
                finite_vals.append(zs_model[np.isfinite(zs_model)])

        all_finite = np.concatenate([v for v in finite_vals if v.size > 0]) \
            if any(v.size > 0 for v in finite_vals) else np.array([0.0, 1.0])
        vmin, vmax = float(np.min(all_finite)), float(np.max(all_finite))
        if vmin == vmax:
            vmax = vmin + 1.0

        cmap_exp = plt.get_cmap('viridis')
        norm = plt.Normalize(vmin=vmin, vmax=vmax)

        # Experimental surface: semi-transparent so the model surface (if
        # shown) remains visible through it, and vice versa.
        exp_alpha = 0.65 if (show_model and zs_model is not None) else 0.95
        zs_exp[:, -1] = zs_exp[:, 0]
        surf_exp = ax.plot_surface(
            xs, ys, zs_exp, facecolors=cmap_exp(norm(zs_exp)),
            rstride=1, cstride=1, linewidth=0.15, edgecolor='#555555',
            alpha=exp_alpha, shade=False, antialiased=True
        )

        if show_model and zs_model is not None:
            # Theoretical model surface: a contrasting colormap so the two
            # surfaces are visually distinguishable even where they overlap,
            # drawn as a translucent wireframe-like surface on top.
            cmap_model = plt.get_cmap('autumn')
            zs_model[:, -1] = zs_model[:, 0]
            ax.plot_surface(
                xs, ys, zs_model, facecolors=cmap_model(norm(zs_model)),
                rstride=1, cstride=1, linewidth=0.2, edgecolor='#aa0000',
                alpha=0.40, shade=False, antialiased=True
            )
        elif show_model and not structures:
            ax.text2D(0.5, 0.5, 'No theoretical model defined',
                     transform=ax.transAxes, ha='center', va='center',
                     fontsize=9, color='gray')

        ax.set_xlabel('X distance', fontsize=8)
        ax.set_ylabel('Y distance', fontsize=8)
        ax.set_zlabel('Semivariance', fontsize=8)
        ax.set_title('Variogram Map (3D)', fontsize=10)
        ax.tick_params(axis='both', labelsize=7)

        # The Z values (semivariance) are typically orders of magnitude
        # smaller than the X/Y distances (e.g. meters vs a small variance
        # value), so a box_aspect proportional to the real data range makes
        # the surface look almost perfectly flat. Instead, give Z its own
        # full-height visual scale -- same physical size as X and Y -- by
        # using an equal box_aspect (1, 1, 1) and letting matplotlib stretch
        # the Z *axis scale* (not the data) to fill that height. This is the
        # standard way to plot dimensionally-different axes (distance vs.
        # semivariance) so the surface is actually visible.
        try:
            ax.set_box_aspect((1, 1, 1))
        except AttributeError:
            pass  # older matplotlib without set_box_aspect: skip, still functional

        max_r = float(np.nanmax(rr))
        ax.set_xlim(-max_r, max_r)
        ax.set_ylim(-max_r, max_r)
        ax.set_zlim(vmin, vmax)

        sm = plt.cm.ScalarMappable(cmap=cmap_exp, norm=norm)
        sm.set_array([])
        cbar = self.figure_vario_map_3d.colorbar(sm, ax=ax, fraction=0.035, pad=0.08)
        cbar.set_label('Semivariance (experimental)', fontsize=8)
        cbar.ax.tick_params(labelsize=7)

        self.canvas_vario_map_3d.draw()

    def plot_multi_variogram(self, lags, results):
        self.figure_vario.clear()
        ax = self.figure_vario.add_subplot(111)
        colors = ['firebrick', 'navy', 'forestgreen', 'darkorange', 'purple', 'teal']

        show_counts   = self.checkBox_show_counts.isChecked()
        show_boxplot  = self.checkBox_boxplot.isChecked()
        show_h_box    = self.checkBox_h_boxplot.isChecked()
        show_model    = (hasattr(self, 'checkBox_theoretical_model') and
                         self.checkBox_theoretical_model.isChecked())

        model_structures = self.get_models_from_table()
        step          = (lags[1] - lags[0]) if len(lags) > 1 else lags[0]
        max_plot_dist = np.max(lags) * 1.05
        h_fine        = np.linspace(0, max_plot_dist, 300)

        # Accumulate h box-plot data for all directions so we can draw them
        # after the main loop (we need the final y limits first).
        # Each entry: (color, list-of-bxp-stat-dicts, list-of-y-positions)
        h_bxp_per_dir = []

        has_data = False
        for idx, (az, (gamma, counts, gamma_values, h_eff, pair_dists)) in enumerate(results.items()):
            valid_idx = ~np.isnan(gamma)
            if not np.any(valid_idx):
                continue
            has_data  = True
            color     = colors[idx % len(colors)]

            # -- Gamma (Y) box plots -------------------------------------------
            if show_boxplot:
                dir_data, dir_positions, dir_widths = [], [], []
                for k in range(len(lags)):
                    if len(gamma_values[k]) > 0:
                        dir_data.append(gamma_values[k])
                        dir_positions.append(h_eff[k])
                        if show_h_box and len(pair_dists[k]) >= 4:
                            # Width of the gamma box = IQR of the actual pair
                            # distances in this bin, so the user can read both
                            # uncertainties simultaneously.
                            q1_h = np.percentile(pair_dists[k], 25)
                            q3_h = np.percentile(pair_dists[k], 75)
                            iqr_h = max(q3_h - q1_h, step * 0.05)
                            dir_widths.append(iqr_h)
                        else:
                            dir_widths.append(step * 0.25)

                if dir_data:
                    bp = ax.boxplot(
                        dir_data,
                        positions=dir_positions,
                        widths=dir_widths,
                        patch_artist=True,
                        manage_ticks=False,
                        showfliers=False,
                    )
                    for patch in bp['boxes']:
                        patch.set_facecolor(color)
                        patch.set_alpha(0.18)
                        patch.set_edgecolor(color)
                    for element in ('whiskers', 'caps', 'medians'):
                        for line in bp[element]:
                            line.set_color(color)
                            line.set_alpha(0.6)

            # -- Mean variogram curve ------------------------------------------
            ax.plot(
                h_eff[valid_idx], gamma[valid_idx],
                linestyle=':', marker='o', color=color, alpha=0.8,
                label=f"Azimuth {az:.1f}\u00b0 (Exp)",
            )

            # -- Theoretical model curve ---------------------------------------
            if model_structures and show_model:
                ax.plot(
                    h_fine,
                    self.calculate_theoretical_semivariance(h_fine, az, model_structures),
                    linestyle='-', linewidth=2.0, color=color, alpha=0.9,
                    label=f"Azimuth {az:.1f}\u00b0 (Model)",
                )

            # -- Pair count annotations ----------------------------------------
            if show_counts:
                for i, txt in enumerate(counts):
                    if valid_idx[i]:
                        ax.annotate(
                            f"N={int(txt)}", (h_eff[i], gamma[i]),
                            textcoords="offset points", xytext=(0, 8),
                            ha='center', fontsize=7, color=color,
                        )

            # -- Collect h box-plot stats for this direction -------------------
            if show_h_box:
                stats_list = []
                for k in range(len(lags)):
                    d = pair_dists[k]
                    if len(d) < 4:
                        continue
                    d_arr = np.array(d)
                    stats_list.append({
                        'med':     float(np.median(d_arr)),
                        'q1':      float(np.percentile(d_arr, 25)),
                        'q3':      float(np.percentile(d_arr, 75)),
                        'whislo':  float(np.percentile(d_arr,  5)),
                        'whishi':  float(np.percentile(d_arr, 95)),
                        'fliers':  [],
                        'label':   '',
                    })
                if stats_list:
                    h_bxp_per_dir.append((color, stats_list))

        # -- Draw horizontal h box plots below y = 0 ---------------------------
        if show_h_box and h_bxp_per_dir:
            # Determine the height of the gamma region and allocate a strip
            # below y = 0 for each direction's h distribution.
            y_top = ax.get_ylim()[1]
            strip_h   = 0.06 * y_top   # height of one strip in data coords
            strip_gap = 0.01 * y_top   # gap between strips

            for d_idx, (color, stats_list) in enumerate(h_bxp_per_dir):
                # y centre of this direction's strip (negative, below the axis)
                y_center = -(d_idx + 0.5) * (strip_h + strip_gap)
                positions = [y_center] * len(stats_list)

                bp_h = ax.bxp(
                    stats_list,
                    positions=positions,
                    vert=False,               # horizontal box plots
                    widths=strip_h * 0.7,
                    patch_artist=True,
                    manage_ticks=False,
                    showfliers=False,
                )
                for patch in bp_h['boxes']:
                    patch.set_facecolor(color)
                    patch.set_alpha(0.25)
                    patch.set_edgecolor(color)
                for element in ('whiskers', 'caps', 'medians'):
                    for line in bp_h[element]:
                        line.set_color(color)
                        line.set_alpha(0.7)

            # Expand y axis to reveal the h strips and add a separator line.
            total_h_space = len(h_bxp_per_dir) * (strip_h + strip_gap) + strip_gap
            ax.set_ylim(-total_h_space, y_top)
            ax.axhline(0, color='black', linewidth=0.8, linestyle='--', alpha=0.4)

            # Label the h-distribution zone on the right side
            ax.text(
                max_plot_dist * 0.99,
                -total_h_space * 0.5,
                "h distribution",
                ha='right', va='center', fontsize=7,
                color='dimgray', style='italic',
            )

        ax.set_title("Multi-Directional Experimental vs Theoretical Variogram")
        ax.set_xlabel("Lag distance h")
        ax.set_ylabel("Semivariance \u03b3(h)")
        ax.grid(True, linestyle=':', alpha=0.6)
        ax.set_xlim(0, max_plot_dist)
        ax.legend(loc='best', fontsize=8)
        self.figure_vario.tight_layout()
        self.canvas_vario.draw()

