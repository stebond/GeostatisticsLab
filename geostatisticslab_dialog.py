    def calculate_theoretical_semivariance(self, h, azimuth_or_coords, structures):
        """Compute the theoretical semivariance for an array of distances h.

        azimuth_or_coords can be:
          - a float scalar  -> single azimuth in geographic degrees (0=North, clockwise),
            used e.g. for variogram plotting or omnidirectional calls (az=0.0)
          - a tuple (x_from, y_from, x_to, y_to) -> pair coordinates;
            the azimuth is computed automatically per pair as
            arctan2(dX, dY) % 180 (range [0,180) exploiting variogram symmetry)
        """
        h = np.asarray(h, dtype=float)
        # Resolve azimuth: scalar value or per-pair computation from coordinates
        if isinstance(azimuth_or_coords, tuple):
            x_from, y_from, x_to, y_to = azimuth_or_coords
            dx = np.asarray(x_to) - np.asarray(x_from)
            dy = np.asarray(y_to) - np.asarray(y_from)
            azimuth = np.degrees(np.arctan2(dx, dy)) % 180.0  # shape uguale a h
        elif np.isscalar(azimuth_or_coords):
            azimuth = float(azimuth_or_coords)
        else:
            azimuth = np.asarray(azimuth_or_coords, dtype=float)

        gamma = np.zeros_like(h, dtype=float)
        for struct in structures:
            stype = struct['type']
            sill  = struct['sill']
            if sill <= 0:
                continue
            if stype == "Nugget":
                gamma[h > 0] += sill
                continue
            r_major = struct['r1']
            r_minor = struct['r2'] if struct['r2'] > 0 else r_major
            a1      = struct['a1']
            beta    = np.radians(azimuth - a1)
            denom   = np.sqrt((np.cos(beta) / r_major)**2 + (np.sin(beta) / r_minor)**2)
            # r_eff vectorised (works with both a scalar azimuth and a per-pair array)
            r_eff   = np.where(denom > 0, 1.0 / denom, r_major)
            h_ratio = np.where(r_eff > 0, h / r_eff, 0.0)
            if stype == "Spherical":
                gamma += np.where(h_ratio <= 1.0,
                                  sill * (1.5*h_ratio - 0.5*h_ratio**3), sill)
            elif stype == "Exponential":
                gamma += sill * (1.0 - np.exp(-3.0 * h_ratio))
            elif stype == "Gaussian":
                gamma += sill * (1.0 - np.exp(-3.0 * h_ratio**2))
            elif stype == "Linear":
                gamma += sill * h_ratio
            elif stype == "Cubic":
                gamma += np.where(h_ratio <= 1.0,
                                  sill * (7.0*h_ratio**2 - 8.75*h_ratio**3 +
                                          3.5*h_ratio**5 - 0.75*h_ratio**7), sill)
        return gamma
    def get_total_sill(self, structures):
        """Return the total sill of the model (C0)."""
        return sum(struct['sill'] for struct in structures)
    
    def get_covariance(self, h, azimuth, structures):
        """        Converte la semivarianza teorica in covarianza teorica.        C(h) = C(0) - gamma(h)        """
        total_sill = self.get_total_sill(structures)
        gamma = self.calculate_theoretical_semivariance(h, azimuth, structures)
        
        # La covarianza ' il sill totale meno la semivarianza
        # Use np.maximum to avoid tiny negative values caused by floating-point rounding
        return np.maximum(total_sill - gamma, 0.0)
        
        
    def ceil_to_nice_number(self, val):
        if val <= 0: return val
        exponent = int(np.floor(np.log10(val)))
        base = 10 ** exponent
        normalized = val / base
        for step in [1.0, 1.5, 2.0, 2.5, 5.0, 7.5, 10.0]:
            if normalized <= step: return step * base
        return 10.0 * base

    def get_nice_bounds(self, xmin, xmax, ymin, ymax):
        range_x, range_y = xmax - xmin, ymax - ymin
        max_range = max(range_x, range_y)
        if max_range <= 0: return xmin, xmax, ymin, ymax
        exponent = int(np.floor(np.log10(max_range)))
        base = 10 ** exponent
        norm = max_range / base
        step = base / 10.0 if norm < 2 else (base / 5.0 if norm < 5 else base / 2.0)
        return np.floor(xmin / step) * step, np.ceil(xmax / step) * step, np.floor(ymin / step) * step, np.ceil(ymax / step) * step

