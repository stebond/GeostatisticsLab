    # ------------------------------------------------------------------
    # Core NS transform (GSLIB nscore algorithm in Python)
    # ------------------------------------------------------------------

    @staticmethod
    def _nscore(values):
        """Normal score transform of 1-D array `values`.

        Returns
        -------
        ns_values : np.ndarray  - normal scores for each input value (same order)
        table_orig : np.ndarray - sorted original values (transformation table col 0)
        table_ns   : np.ndarray - corresponding normal scores  (transformation table col 1)
        sort_idx   : np.ndarray - indices that sort `values` ascending (table_orig = values[sort_idx]);
                                  callers can use this to reorder companion arrays (e.g. x, y
                                  coordinates) so they stay aligned row-by-row with the sorted table.
        """
        from scipy.stats import norm as scipy_norm

        vals = np.asarray(values, dtype=float)
        n = len(vals)

        # Sort indices; add tiny random jitter to break ties (like GSLIB EPSLON)
        rng = np.random.default_rng(seed=69069)
        jitter = rng.uniform(0, 1e-6, size=n)
        sort_idx = np.argsort(vals + jitter, kind='stable')

        # Cumulative probabilities at midpoints of rank intervals (equal weights)
        ranks = np.arange(1, n + 1)
        cp_hi = ranks / n
        cp_lo = np.concatenate(([0.0], cp_hi[:-1]))
        cp_mid = (cp_hi + cp_lo) / 2.0
        cp_mid = np.clip(cp_mid, 1e-10, 1 - 1e-10)

        ns_sorted = scipy_norm.ppf(cp_mid)

        # Build transformation table (sorted originals -> normal scores)
        table_orig = vals[sort_idx]
        table_ns = ns_sorted

        # Assign NS values back to original order
        ns_values = np.empty(n)
        ns_values[sort_idx] = ns_sorted

        return ns_values, table_orig, table_ns, sort_idx

    @staticmethod
    def _backtr(z_gauss, table_orig, table_ns):
        """Back-transform a Gaussian value to the original domain using the
        transformation table built by _nscore().

        Uses linear interpolation between table entries; clamps to [min, max]
        of the original data for extrapolation (ltail=utail=1 linear).
        """
        z = float(z_gauss)
        # Clamp to table range (linear tail extrapolation = clamp)
        if z <= table_ns[0]:
            return float(table_orig[0])
        if z >= table_ns[-1]:
            return float(table_orig[-1])
        # Linear interpolation
        idx = np.searchsorted(table_ns, z, side='right') - 1
        idx = int(np.clip(idx, 0, len(table_ns) - 2))
        t = (z - table_ns[idx]) / (table_ns[idx + 1] - table_ns[idx])
        return float(table_orig[idx] + t * (table_orig[idx + 1] - table_orig[idx]))

