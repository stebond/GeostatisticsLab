    def _populate_estim_points_combo(self, *args):
        """Populate comboBox_estim_points with all point vector layers in the project."""
        combo = self.comboBox_estim_points
        current_id = combo.currentData()
        combo.blockSignals(True)
        combo.clear()
        for lyr in QgsProject.instance().mapLayers().values():
            if isinstance(lyr, QgsVectorLayer) and lyr.isValid():
                combo.addItem(lyr.name(), lyr.id())
        idx = combo.findData(current_id)
        combo.setCurrentIndex(idx if idx >= 0 else 0)
        combo.blockSignals(False)
        self._suggest_estim_vect_name()

    @staticmethod
    def _unique_layer_name(base_name):
        """Return base_name if no project layer already uses it, else base_name_1,
        base_name_2, ... ."""
        existing = {l.name() for l in QgsProject.instance().mapLayers().values()}
        if base_name not in existing:
            return base_name
        i = 1
        while True:
            candidate = "%s_%d" % (base_name, i)
            if candidate not in existing:
                return candidate
            i += 1

    def _suggest_estim_vect_name(self, *args):
        """Pre-fill lineEdit_estim_vect_name with
        '<comboBox_estim_points layer name>_<selected variable>', adding a
        numeric suffix if that name is already used by a project layer."""
        points_layer_name = self.comboBox_estim_points.currentText().strip()
        field_name = self.mFieldComboBox.currentField()
        if not points_layer_name or not field_name:
            return
        base = "%s_%s" % (points_layer_name, field_name)
        self.lineEdit_estim_vect_name.setText(self._unique_layer_name(base))

    def _run_kriging_on_point_layer(self, layer, field_name, structures,
                                    X_known, Y_known, values,
                                    is_ok, is_sk, is_uk,
                                    search_r_max, search_r_min, search_azimuth,
                                    use_octants, min_per_octant, pts_min, pts_max,
                                    mean_value, sample_variance, NO_VALUE):
        """Estimate at the locations of comboBox_estim_points's layer and create
        a new in-memory point layer with two attributes:
            <field_name>_estimated  (Double) - kriging estimate
            <field_name>_est_var    (Double) - kriging variance
        The new layer is added to the QGIS project in memory only (never
        written to disk)."""
        target_layer_id = self.comboBox_estim_points.currentData()
        target_layer = QgsProject.instance().mapLayer(target_layer_id) if target_layer_id else None
        if target_layer is None:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Could not resolve the selected point layer for estimation.")
            return None

        out_name = self.lineEdit_estim_vect_name.text().strip()
        if not out_name:
            out_name = self._unique_layer_name(
                "%s_%s" % (target_layer.name(), field_name))
        else:
            out_name = self._unique_layer_name(out_name)

        est_field_name = field_name + "_estimated"
        var_field_name = field_name + "_est_var"

        crs_str = target_layer.crs().authid() or target_layer.crs().toWkt()
        out_layer = QgsVectorLayer("Point?crs=%s" % crs_str, out_name, "memory")
        pr = out_layer.dataProvider()
        pr.addAttributes([
            QgsField(est_field_name, QVariant.Double),
            QgsField(var_field_name, QVariant.Double),
        ])
        out_layer.updateFields()
        out_fields = out_layer.fields()
        idx_est = out_fields.indexOf(est_field_name)
        idx_var = out_fields.indexOf(var_field_name)

        target_features = list(target_layer.getFeatures())
        total = len(target_features)
        if total == 0:
            QtWidgets.QMessageBox.warning(self, "No Points",
                "The selected point layer for estimation has no features.")
            return None

        new_features = []
        self.progressBar_kriging.setValue(0)
        for i, feat in enumerate(target_features):
            geom = feat.geometry()
            if geom is None or geom.isNull():
                continue
            pt = geom.asPoint()
            est_val, krig_var = self._estimate_single_point(
                pt.x(), pt.y(), X_known, Y_known, values,
                is_ok, is_sk, is_uk, structures,
                search_r_max, search_r_min, search_azimuth,
                use_octants, min_per_octant, pts_min, pts_max,
                mean_value, sample_variance, NO_VALUE
            )
            new_feat = QgsFeature(out_fields)
            new_feat.setGeometry(QgsGeometry.fromPointXY(pt))
            new_feat.setAttribute(idx_est, None if est_val == NO_VALUE else float(est_val))
            new_feat.setAttribute(idx_var, None if krig_var == NO_VALUE else float(krig_var))
            new_features.append(new_feat)

            if i % 50 == 0:
                self.progressBar_kriging.setValue(int((i + 1) / total * 100))
                QtWidgets.QApplication.processEvents()

        self.progressBar_kriging.setValue(100)

        ok, _ = pr.addFeatures(new_features)
        if not ok:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Failed to add estimated features to the output layer.")
            return None

        out_layer.updateExtents()
        QgsProject.instance().addMapLayer(out_layer)
        return out_layer

    def run_kriging_interpolation(self):
        """Validates parameters, runs the selected 2D Kriging system, and creates an output raster."""
        # 1. Preliminary check: has the user computed or adjusted the variogram model?
        if not self.variogram_computed:
            reply = QtWidgets.QMessageBox.warning(
                self, "Warning: Default Variogram",
                "You haven't computed or adjusted the variogram model for this layer yet.\n"
                "Using the default model parameters will likely produce incorrect estimations.\n\n"
                "Do you want to continue anyway?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No,
                QtWidgets.QMessageBox.No
            )
            if reply == QtWidgets.QMessageBox.No:
                # Switch directly to the Variogram tab (index 1)
                self.tabWidget.setCurrentIndex(1)
                return
        estim_on_points = self.radioButton_estim_points.isChecked()

        if not estim_on_points:
            output_path = self.lineEdit_estimated_layer_name.text()
            if not output_path or not os.path.exists(os.path.dirname(output_path)):
                output_path = self.prompt_save_path()
                if not output_path: return  # User cancelled the dialog

        # 2. Extract input point layer and geometry
        layer = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name:
            QtWidgets.QMessageBox.critical(self, "Error", "Invalid point layer or field chosen.")
            return

        coords, values = [], []
        for f in layer.getFeatures():
            if f.hasGeometry():
                val = f[field_name]
                if val != NULL and val is not None:
                    try:
                        pt = f.geometry().asPoint()
                        coords.append([pt.x(), pt.y()])
                        values.append(float(val))
                    except (ValueError, TypeError): continue
        
        coords, values = np.array(coords), np.array(values)
        num_points = len(values)
        if num_points < 3:
            QtWidgets.QMessageBox.critical(self, "Error", "Kriging requires at least 3 points to build a spatial matrix.")
            return

        # 2b. Check for duplicate XY coordinates
        rounded = np.round(coords, decimals=6)
        unique_coords, inv_idx, counts = np.unique(
            rounded, axis=0, return_inverse=True, return_counts=True
        )
        dup_mask = counts[inv_idx] > 1
        num_dups = int(dup_mask.sum())
        if num_dups > 0:
            dup_xy = np.unique(rounded[dup_mask], axis=0)
            dup_list = "\n".join(f"  ({p[0]:.4f}, {p[1]:.4f})" for p in dup_xy[:10])
            if len(dup_xy) > 10:
                dup_list += f"\n  ... and {len(dup_xy)-10} more"
            reply = QtWidgets.QMessageBox.warning(
                self, "Duplicate Coordinates Detected",
                f"Found {num_dups} point(s) sharing the same XY location:\n{dup_list}\n\n"
                "Duplicate locations cause a singular matrix.\n"
                "Only the first occurrence of each duplicate will be kept.\n\n"
                "Do you want to continue?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No,
                QtWidgets.QMessageBox.No
            )
            if reply == QtWidgets.QMessageBox.No:
                return
            # Keep only the first occurrence of each duplicated location
            _, first_idx = np.unique(rounded, axis=0, return_index=True)
            first_idx = np.sort(first_idx)
            coords = coords[first_idx]
            values = values[first_idx]
            num_points = len(values)

        # 3. Grid parameters from Tab 3
        xmin = self.doubleSpinBox_xmin.value()
        ymin = self.doubleSpinBox_ymin.value()
        Nx = self.spinBox_Nx.value()
        Ny = self.spinBox_Ny.value()
        dx = self.doubleSpinBox_dx.value()
        dy = self.doubleSpinBox_dy.value()
        # 3b. Search ellipse parameters and point filter settings
        search_r_max   = self.doubleSpinBox_searchR_max.value()
        search_r_min   = self.doubleSpinBox_searchR_min.value()
        search_azimuth = self.doubleSpinBox_search_azimuth.value()  # gradi geografici
        use_octants    = self.checkBox_octant_search.isChecked()
        min_per_octant = self.spinBox_min_point_per_octant.value()
        pts_min        = self.spinBox_kriging_num_points_min.value()
        pts_max        = self.spinBox_kriging_num_points_max.value()
        NO_VALUE       = float(self.doubleSpinBox_nodata_value.value()) if hasattr(self, "doubleSpinBox_nodata_value") else -9999.0

        # Set up the kriging type
        is_uk = self.radioButton_UniversalKriging.isChecked()
        is_sk = self.radioButton_SimpleKriging.isChecked()
        is_ok = self.radioButton_OrdinaryKriging.isChecked()
        if is_ok:
            string_name_type="ok"
        elif is_uk:
            string_name_type="uk"
        else:
            string_name_type="sk"
        # 4. Output raster filename management (grid mode only)
        if not estim_on_points:
            base_dir = os.path.dirname(layer.source()) if os.path.exists(layer.source()) else QgsProject.instance().homePath()
            if not os.path.isdir(base_dir):
                base_dir = QtCore.QDir.tempPath()

            requested_name = self.lineEdit_estimated_layer_name.text().strip()
            if not requested_name:
                requested_name = f"{field_name}_{string_name_type}"

            output_filename = f"{requested_name}.tif"
            output_path = os.path.join(base_dir, output_filename)

            # Check for existing file conflicts on disk
            if os.path.exists(output_path):
                reply = QtWidgets.QMessageBox.question(
                    self, "File Existing",
                    f"The file '{output_filename}' already exists in the project path.\n"
                    "Do you want to overwrite it?\n\n(Click 'No' to create a new unique indexed copy)",
                    QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No
                )
                if reply == QtWidgets.QMessageBox.No:
                    counter = 1
                    while True:
                        output_filename = f"{requested_name}_{counter}.tif"
                        output_path = os.path.join(base_dir, output_filename)
                        if not os.path.exists(output_path):
                            break
                        counter += 1
                    # Update the UI with the auto-incremented filename
                    self.lineEdit_estimated_layer_name.setText(output_filename.replace(".tif", ""))

        # 5. Build the theoretical semivariance model
        structures = self.get_models_from_table()
        
        # Compute sample variance for Simple Kriging (prior variance)
        sample_variance = float(np.var(values))
        
        # get_cov_or_semi: local wrapper delegating to calculate_theoretical_semivariance.
        # Coordinates are passed as a tuple so the per-pair azimuth is computed automatically.
        def get_cov_or_semi(h_dists, x_from, y_from, x_to, y_to):
            return self.calculate_theoretical_semivariance(
                h_dists, (x_from, y_from, x_to, y_to), structures
            )

        # 6. Build the known-points matrix (A)
        # Compute pairwise distances between all known points
        X_known = coords[:, 0]
        Y_known = coords[:, 1]
        
        # Geometric distances between point pairs
        dists_pts = np.sqrt((X_known[:, None] - X_known[None, :])**2 + (Y_known[:, None] - Y_known[None, :])**2)
        # Gamma_A: true azimuth computed for each pair (i,j) of sampled points
        Gamma_A = get_cov_or_semi(
            dists_pts,
            X_known[:, None], Y_known[:, None],  # "from" coordinates (row i)
            X_known[None, :], Y_known[None, :]   # "to"   coordinates (column j)
        )

        
            
        # mean_value: global mean used by Simple Kriging
        mean_value = self.doubleSpinBox_mean_sk.value()
        # Note: matrix A and vector B are now built locally for each estimation node,
        # using only the points selected by the search ellipse.
        # The global Gamma_A (computed above) is kept for potential future use
        # (e.g. diagnostics) but is no longer inverted globally.

        # -- Estimation target: existing point layer instead of a grid --------
        if estim_on_points:
            out_layer = self._run_kriging_on_point_layer(
                layer, field_name, structures,
                X_known, Y_known, values,
                is_ok, is_sk, is_uk,
                search_r_max, search_r_min, search_azimuth,
                use_octants, min_per_octant, pts_min, pts_max,
                mean_value, sample_variance, NO_VALUE
            )
            if out_layer is not None:
                QtWidgets.QMessageBox.information(
                    self, "Done",
                    "Point-based kriging complete.\n"
                    "New layer '%s' added to the project (in memory).\n"
                    "  %s_estimated : kriging estimate\n"
                    "  %s_est_var   : kriging variance" % (
                        out_layer.name(), field_name, field_name)
                )
            return

        # 7. Estimation loop over all grid nodes (final raster matrix)
        # Initialise raster matrices. Rows = Ny, Columns = Nx.
        # Note: GIS rasters originate from the top-left corner (Y max, X min)
        raster_data = np.zeros((Ny, Nx), dtype=np.float32)
        variance_data = np.zeros((Ny, Nx), dtype=np.float32) # NUOVA MATRICE
        
        self.progressBar_kriging.setValue(0)
        total_pixels = Ny * Nx
        pixel_count = 0

        # Cell-by-cell estimation loop
        for r in range(Ny):
            # Y coordinate of the raster node centre (top to bottom)
            y_node = (ymin + (Ny - 1 - r) * dy) + (dy / 2.0)
            
            for c in range(Nx):
                # X coordinate of the raster node centre (left to right)
                x_node = (xmin + c * dx) + (dx / 2.0)
                
                # -- Select points inside the search ellipse ----------------------
                dx_pts = X_known - x_node
                dy_pts = Y_known - y_node
                dists_to_node = np.sqrt(dx_pts**2 + dy_pts**2)

                # Rotate into the ellipse coordinate system (azimuth = major-axis direction)
                az_rad = np.radians(search_azimuth)
                dx_rot =  dx_pts * np.cos(az_rad) + dy_pts * np.sin(az_rad)
                dy_rot = -dx_pts * np.sin(az_rad) + dy_pts * np.cos(az_rad)
                r_min_safe = search_r_min if search_r_min > 0 else search_r_max
                in_ellipse = ((dx_rot / search_r_max)**2 + (dy_rot / r_min_safe)**2) <= 1.0
                sel_idx = np.where(in_ellipse)[0]

                # -- Octant search (optional) --------------------------------------
                if use_octants and len(sel_idx) > 0:
                    angles = np.degrees(np.arctan2(dy_pts[sel_idx], dx_pts[sel_idx])) % 360.0
                    octant_ids = (angles // 45).astype(int)  # 0..7
                    kept = []
                    for oct_id in range(8):
                        oct_mask = octant_ids == oct_id
                        oct_pts  = sel_idx[oct_mask]
                        if len(oct_pts) < min_per_octant:
                            continue  # ottante con troppo pochi punti: scartato
                        # Sort by increasing distance and keep the closest
                        oct_sorted = oct_pts[np.argsort(dists_to_node[oct_pts])]
                        kept.append(oct_sorted)
                    sel_idx = np.concatenate(kept) if kept else np.array([], dtype=int)

                # -- Min / max point filter ----------------------------------------
                n_sel = len(sel_idx)
                if n_sel < pts_min:
                    # Too few points: node cannot be estimated
                    raster_data[r, c]   = NO_VALUE
                    variance_data[r, c] = NO_VALUE
                    pixel_count += 1
                    if pixel_count % 500 == 0:
                        self.progressBar_kriging.setValue(int((pixel_count / total_pixels) * 100))
                        QtWidgets.QApplication.processEvents()
                    continue

                if n_sel > pts_max:
                    # Too many points: keep the pts_max closest ones
                    closest = np.argsort(dists_to_node[sel_idx])[:pts_max]
                    sel_idx = sel_idx[closest]
                    n_sel   = pts_max

                # -- Subset dati locali --------------------------------------------
                X_loc  = X_known[sel_idx]
                Y_loc  = Y_known[sel_idx]
                V_loc  = values[sel_idx]
                D_loc  = dists_to_node[sel_idx]

                # Local matrix A (rebuilt using the selected points only)
                dists_loc = np.sqrt((X_loc[:, None]-X_loc[None,:])**2 +
                                    (Y_loc[:, None]-Y_loc[None,:])**2)
                Gamma_A_loc = get_cov_or_semi(
                    dists_loc,
                    X_loc[:, None], Y_loc[:, None],
                    X_loc[None, :], Y_loc[None, :]
                )
                Gamma_B = get_cov_or_semi(
                    D_loc,
                    X_loc, Y_loc,
                    x_node, y_node
                )

                if is_ok:
                    A_loc = np.zeros((n_sel + 1, n_sel + 1))
                    A_loc[:n_sel, :n_sel] = Gamma_A_loc
                    A_loc[n_sel, :n_sel]  = 1.0
                    A_loc[:n_sel, n_sel]  = 1.0
                    B_vec = np.zeros(n_sel + 1)
                    B_vec[:n_sel] = Gamma_B
                    B_vec[n_sel]  = 1.0
                    try:
                        sol = np.linalg.solve(A_loc, B_vec)
                    except np.linalg.LinAlgError:
                        raster_data[r, c] = NO_VALUE; variance_data[r, c] = NO_VALUE
                        pixel_count += 1; continue
                    weights     = sol[:n_sel]
                    lagrange_mu = sol[n_sel]
                    estimated_val = np.sum(weights * V_loc)
                    kriging_var   = np.sum(weights * Gamma_B) + lagrange_mu
                elif is_uk:
                    # Universal Kriging with linear trend (3 constraints: 1 + X + Y)
                    # Matrix size: n_sel + 3  (NOT +4 - there is no 4th constraint)
                    nc = n_sel + 3
                    A_loc = np.zeros((nc, nc))
                    A_loc[:n_sel, :n_sel]   = Gamma_A_loc
                    # Constraint rows/cols: [n_sel]=unity, [n_sel+1]=X, [n_sel+2]=Y
                    A_loc[n_sel,   :n_sel]  = 1.0
                    A_loc[:n_sel,  n_sel]   = 1.0
                    A_loc[n_sel+1, :n_sel]  = X_loc
                    A_loc[:n_sel,  n_sel+1] = X_loc
                    A_loc[n_sel+2, :n_sel]  = Y_loc
                    A_loc[:n_sel,  n_sel+2] = Y_loc
                    B_vec = np.zeros(nc)
                    B_vec[:n_sel]   = Gamma_B
                    B_vec[n_sel]    = 1.0
                    B_vec[n_sel+1]  = x_node
                    B_vec[n_sel+2]  = y_node
                    try:
                        sol = np.linalg.solve(A_loc, B_vec)
                    except np.linalg.LinAlgError:
                        raster_data[r, c] = NO_VALUE; variance_data[r, c] = NO_VALUE
                        pixel_count += 1; continue
                    weights       = sol[:n_sel]
                    lagrange_mu   = sol[n_sel]      # Lagrange multiplier (unity constraint)
                    estimated_val = np.sum(weights * V_loc)
                    # Kriging variance: w^T * gamma_B + mu0 (unity Lagrange multiplier)
                    kriging_var   = np.sum(weights * Gamma_B) + lagrange_mu
                else:
                    # Simple Kriging: use the local covariance matrix
                    C_loc = self.get_covariance(dists_loc, 0.0, structures)
                    cov_B = self.get_covariance(D_loc, 0.0, structures)
                    try:
                        weights = np.linalg.solve(C_loc, cov_B)
                    except np.linalg.LinAlgError:
                        raster_data[r, c] = NO_VALUE; variance_data[r, c] = NO_VALUE
                        pixel_count += 1; continue
                    estimated_val = mean_value + np.sum(weights * (V_loc - mean_value))
                    kriging_var   = self.get_total_sill(structures) - np.sum(weights * cov_B)

                raster_data[r, c]   = estimated_val
                variance_data[r, c] = max(0.0, kriging_var)
                
                pixel_count += 1
                if pixel_count % 500 == 0:
                    self.progressBar_kriging.setValue(int((pixel_count / total_pixels) * 100))
                    QtWidgets.QApplication.processEvents()

        self.progressBar_kriging.setValue(100)

        # 8. Scrittura fisica del file raster GeoTIFF tramite la libreria standard GDAL
        try:
            from osgeo import gdal, osr
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "GDAL Error", "GDAL library not available in QGIS environment.")
            return

        driver = gdal.GetDriverByName('GTiff')
        # Create the raster dataset: File, Columns (Nx), Rows (Ny), Bands (1), Data type (Float32)
        dataset = driver.Create(output_path, Nx, Ny, 1, gdal.GDT_Float32)
        
        # Set raster GeoTransformation
        # [X_min, X_resolution, X_rotation, Y_max, Y_rotation, -Y_resolution]
        # Negative dy because the raster origin is at the top-left corner
        geotransform = (xmin, dx, 0.0, ymin + Ny * dy, 0.0, -dy)
        dataset.SetGeoTransform(geotransform)
        
        # Assign the same CRS as the input point vector layer
        if layer.crs().isValid():
            srs = osr.SpatialReference()
            srs.ImportFromWkt(layer.crs().toWkt())
            dataset.SetProjection(srs.ExportToWkt())
            
        band = dataset.GetRasterBand(1)
        band.WriteArray(raster_data)
        band.FlushCache()
        dataset = None # Chiude e salva fisicamente il file sul disco
        # Prepara il percorso
        var_path = output_path.replace(".tif", "_var.tif")
        
        # Create the variance dataset
        dataset_var = driver.Create(var_path, Nx, Ny, 1, gdal.GDT_Float32)
        dataset_var.SetGeoTransform(geotransform)
        if layer.crs().isValid():
            dataset_var.SetProjection(srs.ExportToWkt())
            
        band_var = dataset_var.GetRasterBand(1)
        band_var.WriteArray(variance_data)
        band_var.FlushCache()
        dataset_var = None
        # 9. Automatically load the computed raster into QGIS
        for path in [output_path, var_path]:
            file_info = QtCore.QFileInfo(path)
            raster_layer = QgsRasterLayer(path, file_info.completeBaseName())
            if raster_layer.isValid():
                QgsProject.instance().addMapLayer(raster_layer)
            else:
                QtWidgets.QMessageBox.critical(self, "Error", "Failed to load the generated raster: %s" % path)

        # Update comboBox_raster so the new kriging rasters are available
        self._populate_raster_combo()
        QtWidgets.QMessageBox.information(self, "Done", "Kriging complete. Rasters loaded.")

    def _estimate_single_point(self, x_node, y_node, X_known, Y_known, values,
                                   is_ok, is_sk, is_uk, structures,
                                   search_r_max, search_r_min, search_azimuth,
                                   use_octants, min_per_octant, pts_min, pts_max,
                                   mean_value, sample_variance, NO_VALUE,
                                   exclude_index=None, return_details=False):
        """Estimate the value at (x_node, y_node) using the surrounding known points.

        If exclude_index is set, that point is removed from the candidate set
        before any search/filter step (used for leave-one-out cross-validation).

        Returns (estimated_value, kriging_variance) by default.
        If return_details=True, returns a third element: a dict with
            'neighbor_idx'  : original indices (into X_known/Y_known/values)
                              of the points actually used in the estimation
            'neighbor_x'    : their X coordinates
            'neighbor_y'    : their Y coordinates
            'weights'       : kriging weights assigned to each neighbor
            'candidate_idx' : original indices of ALL points inside the search
                              ellipse (used or not, e.g. discarded by octant/
                              max-points filtering)
        or None if the point could not be estimated.
        """
        structures = structures  # already resolved by caller

        # Build a local wrapper for the semivariance (identical to the one in run_kriging)
        def get_cov_or_semi(h_dists, x_from, y_from, x_to, y_to):
            return self.calculate_theoretical_semivariance(
                h_dists, (x_from, y_from, x_to, y_to), structures
            )

        # -- Pool of candidate points (optionally excluding one) ---------------
        n_all = len(values)
        all_idx = np.arange(n_all)
        if exclude_index is not None:
            all_idx = all_idx[all_idx != exclude_index]

        X_pool = X_known[all_idx]
        Y_pool = Y_known[all_idx]
        V_pool = values[all_idx]

        # -- Search ellipse ----------------------------------------------------
        dx_pts = X_pool - x_node
        dy_pts = Y_pool - y_node
        dists  = np.sqrt(dx_pts**2 + dy_pts**2)

        az_rad     = np.radians(search_azimuth)
        dx_rot     =  dx_pts * np.cos(az_rad) + dy_pts * np.sin(az_rad)
        dy_rot     = -dx_pts * np.sin(az_rad) + dy_pts * np.cos(az_rad)
        r_min_safe = search_r_min if search_r_min > 0 else search_r_max
        in_ellipse = ((dx_rot / search_r_max)**2 + (dy_rot / r_min_safe)**2) <= 1.0
        sel        = np.where(in_ellipse)[0]
        candidate_idx = all_idx[sel]  # all points inside the ellipse, pre-filtering

        # -- Octant search -----------------------------------------------------
        if use_octants and len(sel) > 0:
            angles     = np.degrees(np.arctan2(dy_pts[sel], dx_pts[sel])) % 360.0
            oct_ids    = (angles // 45).astype(int)
            kept       = []
            for oct_id in range(8):
                oct_pts = sel[oct_ids == oct_id]
                if len(oct_pts) < min_per_octant:
                    continue
                kept.append(oct_pts[np.argsort(dists[oct_pts])])
            sel = np.concatenate(kept) if kept else np.array([], dtype=int)

        # -- Min / max filter --------------------------------------------------
        n_sel = len(sel)
        if n_sel < pts_min:
            return (NO_VALUE, NO_VALUE, None) if return_details else (NO_VALUE, NO_VALUE)

        if n_sel > pts_max:
            sel   = sel[np.argsort(dists[sel])[:pts_max]]
            n_sel = pts_max

        # -- Local subset ------------------------------------------------------
        X_loc = X_pool[sel]
        Y_loc = Y_pool[sel]
        V_loc = V_pool[sel]
        D_loc = dists[sel]

        dists_loc   = np.sqrt((X_loc[:, None]-X_loc[None,:])**2 +
                              (Y_loc[:, None]-Y_loc[None,:])**2)
        Gamma_A_loc = get_cov_or_semi(
            dists_loc,
            X_loc[:, None], Y_loc[:, None],
            X_loc[None, :], Y_loc[None, :]
        )
        Gamma_B = get_cov_or_semi(D_loc, X_loc, Y_loc, x_node, y_node)

        # -- Solve the kriging system ------------------------------------------
        try:
            if is_ok:
                nc    = n_sel + 1
                A_loc = np.zeros((nc, nc))
                A_loc[:n_sel, :n_sel] = Gamma_A_loc
                A_loc[n_sel,  :n_sel] = 1.0
                A_loc[:n_sel,  n_sel] = 1.0
                B_vec = np.zeros(nc)
                B_vec[:n_sel] = Gamma_B
                B_vec[n_sel]  = 1.0
                sol           = np.linalg.solve(A_loc, B_vec)
                weights       = sol[:n_sel]
                lagrange_mu   = sol[n_sel]
                est_val       = np.sum(weights * V_loc)
                krig_var      = np.sum(weights * Gamma_B) + lagrange_mu

            elif is_uk:
                nc    = n_sel + 3
                A_loc = np.zeros((nc, nc))
                A_loc[:n_sel, :n_sel]   = Gamma_A_loc
                A_loc[n_sel,   :n_sel]  = 1.0
                A_loc[:n_sel,   n_sel]  = 1.0
                A_loc[n_sel+1, :n_sel]  = X_loc
                A_loc[:n_sel,  n_sel+1] = X_loc
                A_loc[n_sel+2, :n_sel]  = Y_loc
                A_loc[:n_sel,  n_sel+2] = Y_loc
                B_vec = np.zeros(nc)
                B_vec[:n_sel]  = Gamma_B
                B_vec[n_sel]   = 1.0
                B_vec[n_sel+1] = x_node
                B_vec[n_sel+2] = y_node
                sol            = np.linalg.solve(A_loc, B_vec)
                weights        = sol[:n_sel]
                lagrange_mu    = sol[n_sel]
                est_val        = np.sum(weights * V_loc)
                krig_var       = np.sum(weights * Gamma_B) + lagrange_mu

            else:  # Simple Kriging
                C_loc   = self.get_covariance(dists_loc, 0.0, structures)
                cov_B   = self.get_covariance(D_loc,     0.0, structures)
                weights = np.linalg.solve(C_loc, cov_B)
                est_val = mean_value + np.sum(weights * (V_loc - mean_value))
                krig_var = self.get_total_sill(structures) - np.sum(weights * cov_B)

        except np.linalg.LinAlgError:
            return (NO_VALUE, NO_VALUE, None) if return_details else (NO_VALUE, NO_VALUE)

        if return_details:
            details = {
                'neighbor_idx':   all_idx[sel],
                'neighbor_x':     X_loc,
                'neighbor_y':     Y_loc,
                'weights':        weights,
                'candidate_idx':  candidate_idx,
            }
            return float(est_val), float(max(0.0, krig_var)), details

        return float(est_val), float(max(0.0, krig_var))


    # =========================================================================
    # Cross-validation neighbourhood optimization
    # =========================================================================

    # Definition of the seven optimizable parameters: widget name, display
    # label, type ('int', 'float', 'bool') and automatic bounds.
    # Auto bounds for the two search radii are computed at run-time from the
    # data extent; all other auto bounds are hard-coded here.
    _CK_OPT_PARAMS = [
        {'label': 'Min points',     'widget': 'spinBox_kriging_num_points_min',  'type': 'int',   'auto_min': 2,   'auto_max': 10},
        {'label': 'Max points',     'widget': 'spinBox_kriging_num_points_max',  'type': 'int',   'auto_min': 4,   'auto_max': 25},
        {'label': 'Search R max',   'widget': 'doubleSpinBox_searchR_max',       'type': 'float', 'auto_min': None,'auto_max': None},
        {'label': 'Search R min',   'widget': 'doubleSpinBox_searchR_min',       'type': 'float', 'auto_min': None,'auto_max': None},
        {'label': 'Azimuth',        'widget': 'doubleSpinBox_search_azimuth',    'type': 'float', 'auto_min': 0,   'auto_max': 180},
        {'label': 'Octant search',  'widget': 'checkBox_octant_search',          'type': 'bool',  'auto_min': 0,   'auto_max': 1},
        {'label': 'Min pts/octant', 'widget': 'spinBox_min_point_per_octant',    'type': 'int',   'auto_min': 1,   'auto_max': 4},
    ]
    _CK_OPT_COLS = ['Parameter', 'Current value', 'Use', 'Auto', 'Min', 'Max']
    # Column indices
    _COL_NAME, _COL_CUR, _COL_USE, _COL_AUTO, _COL_MIN, _COL_MAX = 0, 1, 2, 3, 4, 5

    def _setup_ck_optimization_table(self):
        """Populate tableView_cross_kriging_neigh_optimization with one row per
        optimizable neighbourhood parameter.

        Columns:
          0 - Parameter name (read-only)
          1 - Current value  (read-only, refreshed at optimization time)
          2 - Use  (QCheckBox, checked = include in search, default ON)
          3 - Auto (QCheckBox, checked = plugin chooses bounds automatically)
          4 - Min  (editable QTableWidgetItem, enabled only when Auto is OFF)
          5 - Max  (editable QTableWidgetItem, enabled only when Auto is OFF)
        """
        tw = self.tableView_cross_kriging_neigh_optimization
        # tableView_cross_kriging_neigh_optimization must actually be a
        # QTableWidget (not a QTableView) to support setCellWidget.
        # If the designer placed a QTableView, swap it here at run-time.
        if isinstance(tw, QtWidgets.QTableView) and not isinstance(tw, QtWidgets.QTableWidget):
            parent  = tw.parentWidget()
            geom    = tw.geometry()
            new_tw  = QtWidgets.QTableWidget(parent)
            new_tw.setGeometry(geom)
            new_tw.setObjectName('tableView_cross_kriging_neigh_optimization')
            new_tw.show()
            tw.hide()
            self.tableView_cross_kriging_neigh_optimization = new_tw
            tw = new_tw

        n_rows = len(self._CK_OPT_PARAMS)
        tw.setRowCount(n_rows)
        tw.setColumnCount(len(self._CK_OPT_COLS))
        tw.setHorizontalHeaderLabels(self._CK_OPT_COLS)
        tw.horizontalHeader().setStretchLastSection(True)
        tw.verticalHeader().setVisible(False)
        tw.setEditTriggers(QtWidgets.QAbstractItemView.AllEditTriggers)

        for row, param in enumerate(self._CK_OPT_PARAMS):
            # -- Col 0: parameter name ----------------------------------------
            name_item = QtWidgets.QTableWidgetItem(param['label'])
            name_item.setFlags(QtCore.Qt.ItemIsEnabled)
            tw.setItem(row, self._COL_NAME, name_item)

            # -- Col 1: current value -----------------------------------------
            widget_obj = getattr(self, param['widget'], None)
            cur_val    = self._ck_read_widget(widget_obj, param['type'])
            cur_item   = QtWidgets.QTableWidgetItem(str(cur_val))
            cur_item.setFlags(QtCore.Qt.ItemIsEnabled)
            tw.setItem(row, self._COL_CUR, cur_item)

            # -- Col 2: Use checkbox ------------------------------------------
            chk_use = QtWidgets.QCheckBox()
            chk_use.setChecked(True)
            chk_use.setToolTip("Include this parameter in the optimization search")
            use_cell = QtWidgets.QWidget()
            use_lay  = QtWidgets.QHBoxLayout(use_cell)
            use_lay.addWidget(chk_use); use_lay.setAlignment(QtCore.Qt.AlignCenter)
            use_lay.setContentsMargins(0, 0, 0, 0)
            tw.setCellWidget(row, self._COL_USE, use_cell)

            # -- Col 3: Auto checkbox -----------------------------------------
            chk_auto = QtWidgets.QCheckBox()
            chk_auto.setChecked(True)
            chk_auto.setToolTip("Let the plugin set the search bounds automatically")
            auto_cell = QtWidgets.QWidget()
            auto_lay  = QtWidgets.QHBoxLayout(auto_cell)
            auto_lay.addWidget(chk_auto); auto_lay.setAlignment(QtCore.Qt.AlignCenter)
            auto_lay.setContentsMargins(0, 0, 0, 0)
            tw.setCellWidget(row, self._COL_AUTO, auto_cell)

            # -- Col 4 & 5: Min / Max -----------------------------------------
            for col in (self._COL_MIN, self._COL_MAX):
                item = QtWidgets.QTableWidgetItem("")
                # Grayed out until Auto is unchecked
                item.setFlags(QtCore.Qt.ItemIsEnabled)
                item.setForeground(QtGui.QBrush(QtGui.QColor(180, 180, 180)))
                tw.setItem(row, col, item)

            # Wire Auto toggle to enable/disable Min & Max cells
            def _make_toggle(r, chk_a, chk_u):
                def _toggle(_=None):
                    locked = chk_a.isChecked() or not chk_u.isChecked()
                    for c in (self._COL_MIN, self._COL_MAX):
                        it = self.tableView_cross_kriging_neigh_optimization.item(r, c)
                        if it is None:
                            continue
                        if locked:
                            it.setFlags(QtCore.Qt.ItemIsEnabled)
                            it.setForeground(QtGui.QBrush(QtGui.QColor(180, 180, 180)))
                        else:
                            it.setFlags(QtCore.Qt.ItemIsEnabled | QtCore.Qt.ItemIsEditable |
                                        QtCore.Qt.ItemIsSelectable)
                            it.setForeground(QtGui.QBrush(QtGui.QColor(0, 0, 0)))
                return _toggle
            _fn = _make_toggle(row, chk_auto, chk_use)
            chk_auto.stateChanged.connect(_fn)
            chk_use.stateChanged.connect(_fn)

        tw.resizeColumnsToContents()

    @staticmethod
    def _ck_read_widget(widget, ptype):
        """Read the current value of a GUI widget as the correct Python type."""
        if widget is None:
            return 0
        if ptype == 'bool':
            return bool(widget.isChecked())
        elif ptype == 'int':
            return int(widget.value())
        else:
            return float(widget.value())

    @staticmethod
    def _ck_sample(ptype, lo, hi):
        """Draw one random sample within [lo, hi] with the correct type."""
        import random
        if ptype == 'bool':
            return bool(random.random() < 0.5)
        elif ptype == 'int':
            return random.randint(int(lo), int(hi))
        else:
            return lo + random.random() * (hi - lo)

    def _run_cv_sse(self, coords, values, structures,
                    is_ok, is_sk, is_uk,
                    search_r_max, search_r_min, search_azimuth,
                    use_octants, min_per_octant, pts_min, pts_max,
                    mean_value, sample_variance, NO_VALUE=-9999.0):
        """Leave-one-out cross-validation; returns (SSE, n_valid).

        If any point cannot be estimated (returns NO_VALUE), the function
        immediately returns (inf, n_valid_so_far) so that configurations
        which skip points are never selected as best during optimization.
        This prevents a spurious SSE improvement caused by fewer pairs
        rather than a genuinely better neighbourhood geometry.
        """
        n = len(values)
        X = coords[:, 0]
        Y = coords[:, 1]
        sse, n_valid = 0.0, 0
        for i in range(n):
            est, _var = self._estimate_single_point(
                float(X[i]), float(Y[i]), X, Y, values,
                is_ok, is_sk, is_uk, structures,
                search_r_max, search_r_min, search_azimuth,
                use_octants, min_per_octant, pts_min, pts_max,
                mean_value, sample_variance, NO_VALUE,
                exclude_index=i, return_details=False,
            )
            if est == NO_VALUE:
                # At least one point could not be estimated: penalise heavily
                # so this candidate is never chosen as the optimum.
                return float('inf'), n_valid
            sse    += (float(values[i]) - est) ** 2
            n_valid += 1
        return sse, n_valid

    def run_ck_neighborhood_optimization(self):
        """Random-search optimization of the kriging neighbourhood parameters
        by minimizing the leave-one-out cross-validation SSE.

        Only configurations where ALL data points can be estimated are
        considered valid. Any candidate that leaves at least one point
        unestimated receives a penalty of inf and is discarded, preventing
        a spurious SSE reduction caused by skipping difficult points.

        Algorithm
        ---------
        1. Read which parameters to optimize and their bounds from the
           optimization table.
        2. Run N_ITER random trials, each with a random combination of the
           active parameters sampled within their bounds.
        3. For each trial evaluate the LOO-CV SSE via _run_cv_sse().
        4. Write the best parameter set back to the neighbourhood widgets.
        """
        import random

        N_ITER = 80  # number of random trials

        # -- 1. Check prerequisites -------------------------------------------
        layer      = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Please select a valid point layer and field first.")
            return
        if not self.variogram_computed:
            QtWidgets.QMessageBox.warning(self, "Warning",
                "No variogram model computed. The optimization will use the current "
                "(possibly default) model parameters.")

        # -- 2. Load data -----------------------------------------------------
        coords, values = [], []
        for f in layer.getFeatures():
            if not f.hasGeometry():
                continue
            val = f[field_name]
            if val == NULL or val is None:
                continue
            try:
                pt = f.geometry().asPoint()
                coords.append([pt.x(), pt.y()])
                values.append(float(val))
            except (ValueError, TypeError):
                continue

        if len(values) < 3:
            QtWidgets.QMessageBox.critical(self, "Error",
                "At least 3 valid points are required.")
            return

        coords  = np.array(coords)
        values  = np.array(values)
        NO_VAL  = -9999.0

        # Kriging type and fixed parameters
        structures      = self.get_models_from_table()
        sample_variance = float(np.var(values))
        mean_value      = self.doubleSpinBox_mean_sk.value()
        is_uk = self.radioButton_UniversalKriging.isChecked()
        is_sk = self.radioButton_SimpleKriging.isChecked()
        is_ok = self.radioButton_OrdinaryKriging.isChecked()

        # -- 3. Read optimization table settings ------------------------------
        tw = self.tableView_cross_kriging_neigh_optimization

        # Compute automatic bounds for the two radii from the data extent
        x_extent = float(np.max(coords[:, 0]) - np.min(coords[:, 0]))
        y_extent = float(np.max(coords[:, 1]) - np.min(coords[:, 1]))
        diag     = float(np.sqrt(x_extent**2 + y_extent**2))
        r_auto_min = diag * 0.05
        r_auto_max = diag * 0.50

        # Fill auto bounds for radius rows
        for param in self._CK_OPT_PARAMS:
            if param['widget'] == 'doubleSpinBox_searchR_max':
                param['auto_min'] = r_auto_min
                param['auto_max'] = r_auto_max
            elif param['widget'] == 'doubleSpinBox_searchR_min':
                param['auto_min'] = r_auto_min * 0.1
                param['auto_max'] = r_auto_max

        # Read current fixed values (used when Use=OFF)
        fixed = {}
        for param in self._CK_OPT_PARAMS:
            w = getattr(self, param['widget'], None)
            fixed[param['widget']] = self._ck_read_widget(w, param['type'])

        # Build per-parameter config: {widget_name: {'use', 'lo', 'hi', 'type'}}
        param_cfg = {}
        for row, param in enumerate(self._CK_OPT_PARAMS):
            wname = param['widget']
            ptype = param['type']

            # Read Use checkbox
            use_cell = tw.cellWidget(row, self._COL_USE)
            chk_use  = use_cell.findChild(QtWidgets.QCheckBox) if use_cell else None
            use      = chk_use.isChecked() if chk_use else False

            if not use:
                param_cfg[wname] = {'use': False, 'type': ptype}
                continue

            # Read Auto checkbox
            auto_cell = tw.cellWidget(row, self._COL_AUTO)
            chk_auto  = auto_cell.findChild(QtWidgets.QCheckBox) if auto_cell else None
            auto      = chk_auto.isChecked() if chk_auto else True

            if auto:
                lo = param['auto_min']
                hi = param['auto_max']
            else:
                try:
                    lo = float(tw.item(row, self._COL_MIN).text())
                    hi = float(tw.item(row, self._COL_MAX).text())
                except (ValueError, AttributeError):
                    lo = param['auto_min']
                    hi = param['auto_max']

            if lo is None or hi is None:
                lo, hi = r_auto_min, r_auto_max
            if hi < lo:
                lo, hi = hi, lo

            param_cfg[wname] = {'use': True, 'lo': lo, 'hi': hi, 'type': ptype}

        # -- 4. Update "Current value" column before starting -----------------
        for row, param in enumerate(self._CK_OPT_PARAMS):
            cur_item = tw.item(row, self._COL_CUR)
            if cur_item is not None:
                cur_item.setText(str(fixed[param['widget']]))

        # -- 5. Build the Latin Hypercube sample plan -------------------------
        # LHS guarantees that each parameter's range is uniformly covered:
        # the [lo, hi] interval is divided into N_ITER equal strata and
        # exactly one sample is drawn from each stratum.  Columns are then
        # shuffled independently so that no two rows share the same stratum
        # on every dimension simultaneously.  This gives far better space-
        # filling coverage than pure Monte Carlo with the same budget.
        #
        # Candidate 0 is always the CURRENT parameter values (warm-start):
        # this guarantees the optimisation never returns a solution worse
        # than what the user already has, even if all random trials fail.

        import random as _random

        # Collect the active parameters in a stable order
        active_params = [(wname, cfg) for wname, cfg in param_cfg.items()
                         if cfg['use']]
        n_active = len(active_params)

        # Build LHS strata for each active parameter
        # strata[p] is a list of N_ITER values, one per stratum, shuffled
        strata = []
        for _wname, cfg in active_params:
            lo, hi, ptype = cfg['lo'], cfg['hi'], cfg['type']
            col = []
            for k in range(N_ITER):
                # sample uniformly within stratum k
                u = (k + _random.random()) / N_ITER
                v = lo + u * (hi - lo)
                if ptype == 'int':
                    v = int(round(lo + u * (hi - lo)))
                    v = max(int(lo), min(int(hi), v))
                elif ptype == 'bool':
                    v = bool(u > 0.5)
                col.append(v)
            _random.shuffle(col)
            strata.append(col)

        # -- 6. Optimisation loop  --------------------------------------------
        best_sse      = float('inf')
        best_params   = None

        fallback_valid  = -1
        fallback_sse    = float('inf')
        fallback_params = None

        pb = self.progressBar_CK_opt if hasattr(self, 'progressBar_CK_opt') else None
        if pb:
            pb.setValue(0)

        for iteration in range(N_ITER + 1):   # +1 for the warm-start
            candidate = dict(fixed)

            if iteration == 0:
                # Warm-start: test the current widget values as-is
                pass
            else:
                # Apply the LHS sample for this iteration (0-indexed into strata)
                idx = iteration - 1
                for p_idx, (wname, _cfg) in enumerate(active_params):
                    candidate[wname] = strata[p_idx][idx]

            # Enforce logical constraints
            pmin = candidate['spinBox_kriging_num_points_min']
            pmax = candidate['spinBox_kriging_num_points_max']
            if pmin > pmax:
                pmin, pmax = pmax, pmin
            candidate['spinBox_kriging_num_points_min'] = pmin
            candidate['spinBox_kriging_num_points_max'] = pmax

            rmax = candidate['doubleSpinBox_searchR_max']
            rmin = candidate['doubleSpinBox_searchR_min']
            if rmin > rmax:
                rmin, rmax = rmax, rmin
            candidate['doubleSpinBox_searchR_max'] = rmax
            candidate['doubleSpinBox_searchR_min'] = rmin

            use_oct = bool(candidate['checkBox_octant_search'])
            mpo     = candidate['spinBox_min_point_per_octant']

            sse, n_valid = self._run_cv_sse(
                coords, values, structures,
                is_ok, is_sk, is_uk,
                float(rmax), float(rmin),
                float(candidate['doubleSpinBox_search_azimuth']),
                use_oct, int(mpo),
                int(pmin), int(pmax),
                mean_value, sample_variance, NO_VAL,
            )

            # Level-1: all n points estimated
            if sse < float('inf') and sse < best_sse:
                best_sse    = sse
                best_params = dict(candidate)

            # Level-2 fallback: most points estimated, then lowest SSE
            if n_valid > fallback_valid or (
                    n_valid == fallback_valid and sse < fallback_sse):
                fallback_valid  = n_valid
                fallback_sse    = sse
                fallback_params = dict(candidate)

            if pb:
                pb.setValue(int(iteration / N_ITER * 100))
            if iteration % 5 == 0:
                QtWidgets.QApplication.processEvents()

        if pb:
            pb.setValue(100)

        # -- 6. Select the solution to apply ----------------------------------
        if best_params is not None:
            chosen       = best_params
            chosen_sse   = best_sse
            chosen_valid = len(values)
            level_msg    = "All points were estimated."
        elif fallback_params is not None:
            chosen       = fallback_params
            chosen_sse   = fallback_sse
            chosen_valid = fallback_valid
            level_msg    = (
                f"Warning: no configuration estimated all {len(values)} points.\n"
                f"Best result: {chosen_valid}/{len(values)} points estimated.\n"
                "Consider enlarging the search radius bounds or reducing\n"
                "Min points in the optimization table."
            )
        else:
            QtWidgets.QMessageBox.warning(self, "Optimization",
                "No valid cross-validation result was obtained.\n"
                "Check the variogram model and the parameter bounds.")
            return

        # -- 7. Write best parameters back to the GUI widgets -----------------
        # Temporarily expand spinbox limits so values are never silently
        # clamped: a clamped value would differ from what was tested and
        # could produce unestimated points in the subsequent cross-validation.
        def _set_spinbox(sb, value):
            """Set value on a QSpinBox or QDoubleSpinBox, expanding limits."""
            lo = sb.minimum()
            hi = sb.maximum()
            if value < lo:
                sb.setMinimum(value if isinstance(value, int)
                              else value - abs(value) * 0.01)
            if value > hi:
                sb.setMaximum(value if isinstance(value, int)
                              else value + abs(value) * 0.01)
            sb.setValue(value)

        spinbox_map = {
            'spinBox_kriging_num_points_min':
                (self.spinBox_kriging_num_points_min, int),
            'spinBox_kriging_num_points_max':
                (self.spinBox_kriging_num_points_max, int),
            'doubleSpinBox_searchR_max':
                (self.doubleSpinBox_searchR_max, float),
            'doubleSpinBox_searchR_min':
                (self.doubleSpinBox_searchR_min, float),
            'doubleSpinBox_search_azimuth':
                (self.doubleSpinBox_search_azimuth, float),
            'spinBox_min_point_per_octant':
                (self.spinBox_min_point_per_octant, int),
        }
        for wname, (sb, cast) in spinbox_map.items():
            if wname in chosen:
                try:
                    _set_spinbox(sb, cast(chosen[wname]))
                except Exception:
                    pass

        # Checkbox does not have limits to worry about
        if 'checkBox_octant_search' in chosen:
            try:
                self.checkBox_octant_search.setChecked(
                    bool(chosen['checkBox_octant_search']))
            except Exception:
                pass

        # Update "Current value" column with the winning values
        for row, param in enumerate(self._CK_OPT_PARAMS):
            cur_item = tw.item(row, self._COL_CUR)
            if cur_item is not None:
                cur_item.setText(str(chosen.get(param['widget'], '')))

        QtWidgets.QMessageBox.information(
            self, "Optimization complete",
            f"{level_msg}\n\n"
            f"Cross-validation SSE: "
            f"{'N/A' if chosen_sse == float('inf') else f'{chosen_sse:.6f}'}\n"
            f"Points estimated: {chosen_valid}/{len(values)}\n"
            f"({N_ITER} Latin Hypercube trials + 1 warm-start)\n\n"
            "The kriging neighbourhood widgets have been updated.\n"
            "Run cross-validation again to confirm the result."
        )

    def run_cross_validation(self):
        """Leave-one-out cross-validation: estimate each input point excluding itself,
        then populate tableView_cross_kriging, update label_cross_kriging_mean_error,
        and add a vector layer with the results to the QGIS project.
        """
        # 1. Preliminary check: has the user computed or adjusted the variogram model?
        if not self.variogram_computed:
            reply = QtWidgets.QMessageBox.warning(
                self, "Warning: Default Variogram",
                "You haven't computed or adjusted the variogram model for this layer yet.\n"
                "Using the default model parameters will likely produce incorrect estimations.\n\n"
                "Do you want to continue anyway?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No,
                QtWidgets.QMessageBox.No
            )
            if reply == QtWidgets.QMessageBox.No:
                self.tabWidget.setCurrentIndex(1)
                return

        # -- 2. Read input layer and field ------------------------------------
        layer      = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name:
            QtWidgets.QMessageBox.critical(self, "Error", "Invalid point layer or field.")
            return

        coords, values, fids = [], [], []
        for f in layer.getFeatures():
            if f.hasGeometry():
                val = f[field_name]
                if val != NULL and val is not None:
                    try:
                        pt = f.geometry().asPoint()
                        coords.append([pt.x(), pt.y()])
                        values.append(float(val))
                        fids.append(f.id())
                    except (ValueError, TypeError):
                        continue

        coords = np.array(coords)
        values = np.array(values)
        n      = len(values)
        if n < 3:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Cross-validation requires at least 3 points.")
            return

        X_known = coords[:, 0]
        Y_known = coords[:, 1]

        # -- 2. Read GUI parameters (same as run_kriging_interpolation) -------
        structures     = self.get_models_from_table()
        sample_variance = float(np.var(values))
        mean_value     = self.doubleSpinBox_mean_sk.value()
        is_uk          = self.radioButton_UniversalKriging.isChecked()
        is_sk          = self.radioButton_SimpleKriging.isChecked()
        is_ok          = self.radioButton_OrdinaryKriging.isChecked()
        search_r_max   = self.doubleSpinBox_searchR_max.value()
        search_r_min   = self.doubleSpinBox_searchR_min.value()
        search_azimuth = self.doubleSpinBox_search_azimuth.value()
        use_octants    = self.checkBox_octant_search.isChecked()
        min_per_octant = self.spinBox_min_point_per_octant.value()
        pts_min        = self.spinBox_kriging_num_points_min.value()
        pts_max        = self.spinBox_kriging_num_points_max.value()
        NO_VALUE       = -9999.0

        if is_ok:
            ktype = "ok"
        elif is_uk:
            ktype = "uk"
        else:
            ktype = "sk"

        # -- 3. Leave-one-out loop ---------------------------------------------
        results = []  # list of dicts, one per point
        self._cross_val_details = []  # list of detail-dicts (or None), one per point
        self.progressBar_kriging.setValue(0)
        for i in range(n):
            x_i, y_i, z_i = float(X_known[i]), float(Y_known[i]), float(values[i])
            est, var, details = self._estimate_single_point(
                x_i, y_i, X_known, Y_known, values,
                is_ok, is_sk, is_uk, structures,
                search_r_max, search_r_min, search_azimuth,
                use_octants, min_per_octant, pts_min, pts_max,
                mean_value, sample_variance, NO_VALUE,
                exclude_index=i, return_details=True
            )
            self._cross_val_details.append(details)
            if est == NO_VALUE:
                error = NO_VALUE
                error2 = NO_VALUE
                ratio  = NO_VALUE
            else:
                error  = z_i - est
                error2 = error ** 2
                ratio  = (error2 / var) if var > 0 else NO_VALUE
            results.append({
                'x': x_i, 'y': y_i,
                'true': z_i, 'estimated': est, 'variance': var,
                'error': error, 'error2': error2, 'ratio': ratio,
                'fid': fids[i]
            })
            self.progressBar_kriging.setValue(int((i + 1) / n * 100))
            if i % 10 == 0:
                QtWidgets.QApplication.processEvents()

        self.progressBar_kriging.setValue(100)

        # Store data needed by the neighbor map / weights table navigation
        self._cross_val_results  = results
        self._cross_val_X        = X_known
        self._cross_val_Y        = Y_known
        self._cross_val_values   = values
        self._cross_val_NO_VALUE = NO_VALUE
        self._cross_val_current_idx = 0


        # -- 4. Populate tableView_cross_kriging -------------------------------
        headers = ['X', 'Y', 'True Value', 'Estimated', 'Krig. Variance',
                   'Error', 'Error2', 'Error2/Variance']
        model = QtGui.QStandardItemModel(n, len(headers))
        model.setHorizontalHeaderLabels(headers)

        valid_errors = [r['error'] for r in results if r['error'] != NO_VALUE]
        color_green = QtGui.QColor(198, 239, 206)  # light green
        color_red   = QtGui.QColor(255, 199, 206)  # light red

        for row, r in enumerate(results):
            # Determine row colour based on Error2/Variance (last column)
            ratio_val = r['ratio']
            if ratio_val != NO_VALUE:
                row_color = color_green if ratio_val <= 1.0 else color_red
            else:
                row_color = None

            for col, key in enumerate(['x', 'y', 'true', 'estimated',
                                       'variance', 'error', 'error2', 'ratio']):
                val = r[key]
                txt = f"{val:.6f}" if val != NO_VALUE else "N/A"
                item = QtGui.QStandardItem(txt)
                item.setEditable(False)
                if row_color is not None:
                    item.setBackground(QtGui.QBrush(row_color))
                model.setItem(row, col, item)

        self.tableView_cross_kriging.setModel(model)
        self.tableView_cross_kriging.resizeColumnsToContents()

        # -- 5. Summary labels -------------------------------------------------
        if valid_errors:
            mean_err = sum(valid_errors) / len(valid_errors)
            min_err  = min(valid_errors)
            max_err  = max(valid_errors)
            sse      = sum(e ** 2 for e in valid_errors)
            self.label_cross_kriging_mean_error.setText(
                f"Mean error: {mean_err:.6f}  (n={len(valid_errors)}/{n})"
            )
            self.label_min_max_error.setText(
                f"Min error: {min_err:.6f}    Max error: {max_err:.6f}    SSE: {sse:.6f}"
            )
        else:
            self.label_cross_kriging_mean_error.setText("Mean error: N/A (no valid estimates)")
            self.label_min_max_error.setText("Min error: N/A    Max error: N/A    SSE: N/A")

        # -- 6. Add / update vector layer in QGIS project ---------------------
        # Only proceed if the user wants the vector layer created
        if self.checkBox_create_vector_cross_kriging.isChecked():
            base_name   = f"{field_name}_cross_kriging_{ktype}"
            crs_wkt     = layer.crs().toWkt()
            target_name = base_name

            # Check if a layer with this name already exists
            existing = [l for l in QgsProject.instance().mapLayers().values()
                        if l.name() == target_name]
            if existing:
                reply = QtWidgets.QMessageBox.question(
                    self, "Layer Already Exists",
                    f"A layer named '{target_name}' already exists in the project.\n\n"
                    "Overwrite it, or create a new numbered copy?",
                    QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No | QtWidgets.QMessageBox.Cancel,
                    QtWidgets.QMessageBox.Cancel
                )
                if reply == QtWidgets.QMessageBox.Cancel:
                    return
                elif reply == QtWidgets.QMessageBox.Yes:
                    QgsProject.instance().removeMapLayer(existing[0].id())
                else:
                    # Auto-increment name
                    counter = 1
                    while any(l.name() == f"{base_name}_{counter}"
                              for l in QgsProject.instance().mapLayers().values()):
                        counter += 1
                    target_name = f"{base_name}_{counter}"

            # Create memory vector layer
            crs_wkt = layer.crs().toWkt()
            vl = QgsVectorLayer(f"Point?crs={crs_wkt}", target_name, "memory")
            pr = vl.dataProvider()
            pr.addAttributes([
                QgsField("x",          QVariant.Double),
                QgsField("y",          QVariant.Double),
                QgsField("true_val",   QVariant.Double),
                QgsField("estimated",  QVariant.Double),
                QgsField("krig_var",   QVariant.Double),
                QgsField("error",      QVariant.Double),
                QgsField("error2",     QVariant.Double),
                QgsField("err2_var",   QVariant.Double),
            ])
            vl.updateFields()

            feats = []
            for r in results:
                feat = QgsFeature()
                feat.setGeometry(QgsGeometry.fromPointXY(QgsPointXY(r['x'], r['y'])))
                def _v(k):
                    v = r[k]
                    return None if v == NO_VALUE else v
                feat.setAttributes([
                    r['x'], r['y'],
                    _v('true'), _v('estimated'), _v('variance'),
                    _v('error'), _v('error2'), _v('ratio')
                ])
                feats.append(feat)

            pr.addFeatures(feats)
            vl.updateExtents()
            QgsProject.instance().addMapLayer(vl)

            # -- Apply Turbo graduated colour ramp on the "error" field ------
            from qgis.core import (QgsGraduatedSymbolRenderer, QgsRendererRange,
                                   QgsSymbol, QgsGradientColorRamp,
                                   QgsGradientStop, QgsClassificationEqualInterval)
            from qgis.PyQt.QtGui import QColor

            # Turbo colour ramp (Google / Matplotlib) - 8 anchor stops
            turbo_stops = [
                (0.000, QColor(  48,  18,  59)),
                (0.143, QColor(  75, 120, 168)),
                (0.286, QColor(  35, 196, 186)),
                (0.429, QColor(  98, 230,  81)),
                (0.571, QColor( 218, 220,  24)),
                (0.714, QColor( 253, 141,  33)),
                (0.857, QColor( 220,  57,  20)),
                (1.000, QColor( 122,   4,   3)),
            ]
            gradient_stops = [QgsGradientStop(pos, col)
                              for pos, col in turbo_stops[1:-1]]
            ramp = QgsGradientColorRamp(
                turbo_stops[0][1],   # color1 (start)
                turbo_stops[-1][1],  # color2 (end)
                False,
                gradient_stops
            )

            # Compute min/max of the error field from features
            err_vals = [r['error'] for r in results if r['error'] != NO_VALUE]
            if err_vals:
                err_min, err_max = min(err_vals), max(err_vals)
                n_classes = 10
                step = (err_max - err_min) / n_classes if err_max != err_min else 1.0

                ranges = []
                for k in range(n_classes):
                    lo  = err_min + k * step
                    hi  = err_min + (k + 1) * step
                    t   = k / (n_classes - 1) if n_classes > 1 else 0.5
                    col = ramp.color(t)
                    sym = QgsSymbol.defaultSymbol(vl.geometryType())
                    sym.setColor(col)
                    sym.setSize(3.0)
                    ranges.append(QgsRendererRange(lo, hi,
                                                   sym, f"{lo:.3f} - {hi:.3f}"))

                renderer = QgsGraduatedSymbolRenderer("error", ranges)
                renderer.setSourceColorRamp(ramp)
                vl.setRenderer(renderer)
                vl.triggerRepaint()

            layer_msg = f"Layer '{target_name}' added to the project.\n"
        else:
            layer_msg = "Vector layer not added (checkbox unchecked).\n"

        # -- 7. Scatter plot: True vs Estimated -------------------------------
        self._plot_cross_validation_scatter(results, NO_VALUE)
        self._plot_cross_validation_map(results, NO_VALUE)

