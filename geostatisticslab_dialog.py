    # Slot: back transform a raster
    # ------------------------------------------------------------------

    def run_back_transform(self):
        """Apply GSLIB back-transform to a raster using the NS table from the layer."""
        import os

        # --- inputs ---
        # Layer containing the NS transformation table
        layer = None
        if hasattr(self, 'comboBox_layer_table_nscore'):
            layer_id = self.comboBox_layer_table_nscore.currentData()
            if layer_id:
                layer = QgsProject.instance().mapLayer(layer_id)
        if layer is None:
            layer = self.mMapLayerComboBox.currentLayer()
        if layer is None:
            QtWidgets.QMessageBox.warning(self, "No Layer",
                "Please select the layer containing the NS table in "
                "'NS Score Layer'.")
            return

        # Raster to back-transform
        raster_name = self.comboBox_raster.currentText().strip()
        raster_layer = None
        for lyr in QgsProject.instance().mapLayers().values():
            if lyr.name() == raster_name and isinstance(lyr, QgsRasterLayer):
                raster_layer = lyr
                break
        if raster_layer is None:
            QtWidgets.QMessageBox.warning(self, "Raster Not Found", "Could not find raster '%s' in the project." % raster_name)
            return

        # NS field (e.g. '<variable>_NSC_TRSF'), selected from
        # comboBox_back_transform_table_variable. Its companion
        # original-value column is the same name with the '_NSC_TRSF'
        # suffix stripped off, following run_ns_transform's naming
        # convention.
        ns_field = self.comboBox_back_transform_table_variable.currentText().strip()
        if not ns_field:
            QtWidgets.QMessageBox.warning(self, "No Table Field", "Please select the NS field.")
            return

        suffix = "_NSC_TRSF"
        if ns_field.endswith(suffix):
            orig_field = ns_field[: -len(suffix)]
        else:
            orig_field = None

        layer_field_names = {f.name() for f in layer.fields()}
        if orig_field is None or orig_field not in layer_field_names:
            QtWidgets.QMessageBox.critical(
                self, "Error",
                "Could not find the original-value column matching '%s' "
                "(expected a column named '%s'). The layer may not have "
                "been created by Normal Score Transform, or its columns "
                "were renamed." % (ns_field, orig_field or "<unknown>")
            )
            return

        # The layer's rows are already sorted by ascending original value
        # (run_ns_transform stores them that way), so these two columns
        # together ARE the transformation table -- read them directly,
        # without re-sorting, to preserve the exact original/ns pairing.
        table_orig_list, table_ns_list = [], []
        for feat in layer.getFeatures():
            o_val = feat[orig_field]
            n_val = feat[ns_field]
            if o_val != NULL and o_val is not None and n_val != NULL and n_val is not None:
                try:
                    table_orig_list.append(float(o_val))
                    table_ns_list.append(float(n_val))
                except (TypeError, ValueError):
                    continue

        if len(table_orig_list) < 2:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Could not read a usable transformation table from columns "
                "'%s' / '%s'." % (orig_field, ns_field))
            return

        table_orig = np.array(table_orig_list)
        table_ns   = np.array(table_ns_list)

        # Defensive: _backtr() requires table_ns sorted ascending for
        # np.searchsorted to work correctly. run_ns_transform() already
        # writes the rows in that order, but sort again here in case the
        # layer's row order was changed afterwards (e.g. manual editing,
        # or a save/reload through a format that does not preserve order).
        order = np.argsort(table_ns)
        table_orig = table_orig[order]
        table_ns   = table_ns[order]

        # Output path
        out_name = self.lineEdit_raster_out.text().strip()
        if not out_name:
            out_name = raster_name + "_back_transformed"

        # Determine output directory (same as input raster)
        raster_path = raster_layer.source()
        out_dir = os.path.dirname(raster_path) if os.path.isabs(raster_path) else os.path.expanduser("~")
        if not out_name.endswith(".tif"):
            out_name += ".tif"
        out_path = self._unique_path(os.path.join(out_dir, out_name))

        # --- Process raster band 1 with GDAL ---
        try:
            from osgeo import gdal
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "Dependency Error", "GDAL Python bindings are required.")
            return

        ds_in = gdal.Open(raster_path, gdal.GA_ReadOnly)
        if ds_in is None:
            QtWidgets.QMessageBox.critical(self, "Error", "Cannot open raster with GDAL.")
            return

        band   = ds_in.GetRasterBand(1)
        nodata = band.GetNoDataValue()
        arr    = band.ReadAsArray().astype(np.float64)

        # Back-transform each pixel
        valid_mask = ~np.isnan(arr)
        if nodata is not None:
            valid_mask &= (arr != nodata)

        out_arr = np.full_like(arr, nodata if nodata is not None else np.nan)
        flat_valid = arr[valid_mask]
        bt = np.array([self._backtr(v, table_orig, table_ns) for v in flat_valid])
        out_arr[valid_mask] = bt

        # Write output GeoTIFF
        driver   = gdal.GetDriverByName("GTiff")
        ds_out   = driver.Create(out_path, ds_in.RasterXSize, ds_in.RasterYSize, 1, gdal.GDT_Float64)
        ds_out.SetGeoTransform(ds_in.GetGeoTransform())
        ds_out.SetProjection(ds_in.GetProjection())
        band_out = ds_out.GetRasterBand(1)
        if nodata is not None:
            band_out.SetNoDataValue(nodata)
        band_out.WriteArray(out_arr)
        band_out.FlushCache()
        ds_out = None
        ds_in  = None

        # Load result into QGIS
        result_layer = QgsRasterLayer(out_path, os.path.splitext(os.path.basename(out_path))[0])
        if result_layer.isValid():
            QgsProject.instance().addMapLayer(result_layer)

        QtWidgets.QMessageBox.information(
            self, "Done",
            "Back-transform complete.\nOutput saved to:\n%s" % out_path
        )

