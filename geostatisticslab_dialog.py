    def run_probability_map(self):
        """Compute a probability-of-exceedance (or -of-non-exceedance) map
        from an estimated raster and its kriging-variance raster, assuming
        the estimation error is Gaussian with that variance.

        P(Z > threshold) = 1 - Phi((threshold - estimate) / sigma)   [higher]
        P(Z <= threshold) = Phi((threshold - estimate) / sigma)      [lower]

        where Phi is the standard normal CDF and sigma = sqrt(variance).
        The output is a new in-memory raster (GDAL /vsimem/), never written
        to disk, added to the QGIS project.
        """
        try:
            from osgeo import gdal
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "Dependency Error",
                                           "GDAL Python bindings are required.")
            return
        try:
            from scipy.stats import norm as scipy_norm
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "Dependency Error",
                                           "scipy is required for probability maps.")
            return

        # --- Resolve input rasters from the combos -----------------------------
        est_name = self.comboBox_est_input_probab.currentText().strip()
        var_name = self.comboBox_var_input_probab.currentText().strip()
        if not est_name or not var_name:
            QtWidgets.QMessageBox.warning(self, "Missing Input",
                "Please select both the estimated raster and the variance raster.")
            return

        est_layer = var_layer = None
        for lyr in QgsProject.instance().mapLayers().values():
            if isinstance(lyr, QgsRasterLayer) and lyr.name() == est_name:
                est_layer = lyr
            if isinstance(lyr, QgsRasterLayer) and lyr.name() == var_name:
                var_layer = lyr
        if est_layer is None or var_layer is None:
            QtWidgets.QMessageBox.warning(self, "Raster Not Found",
                "Could not find the selected raster(s) in the project.")
            return

        threshold = self.doubleSpinBox_probab_threshold.value()
        higher = self.radioButton_higher_probab.isChecked()

        # --- Read both rasters with GDAL ----------------------------------------
        ds_est = gdal.Open(est_layer.source(), gdal.GA_ReadOnly)
        ds_var = gdal.Open(var_layer.source(), gdal.GA_ReadOnly)
        if ds_est is None or ds_var is None:
            QtWidgets.QMessageBox.critical(self, "Error", "Cannot open the selected rasters with GDAL.")
            return

        if (ds_est.RasterXSize, ds_est.RasterYSize) != (ds_var.RasterXSize, ds_var.RasterYSize):
            QtWidgets.QMessageBox.critical(self, "Error",
                "The estimated raster and the variance raster do not have the "
                "same dimensions. They must come from the same kriging run.")
            return

        band_est = ds_est.GetRasterBand(1)
        band_var = ds_var.GetRasterBand(1)
        nodata_est = band_est.GetNoDataValue()
        nodata_var = band_var.GetNoDataValue()

        arr_est = band_est.ReadAsArray().astype(np.float64)
        arr_var = band_var.ReadAsArray().astype(np.float64)

        valid = np.isfinite(arr_est) & np.isfinite(arr_var) & (arr_var > 0)
        if nodata_est is not None:
            valid &= (arr_est != nodata_est)
        if nodata_var is not None:
            valid &= (arr_var != nodata_var)

        out_nodata = -9999.0
        prob = np.full(arr_est.shape, out_nodata, dtype=np.float64)

        sigma = np.sqrt(arr_var[valid])
        z = (threshold - arr_est[valid]) / sigma
        if higher:
            prob[valid] = 1.0 - scipy_norm.cdf(z)   # P(Z > threshold)
        else:
            prob[valid] = scipy_norm.cdf(z)         # P(Z <= threshold)

        # --- Output layer name: <variable>_prob_<threshold> ---------------------
        field_name = self.mFieldComboBox.currentField() or est_name
        threshold_str = ("%g" % threshold).replace(".", "_").replace("-", "m")
        suggested_name = "%s_prob_%s" % (field_name, threshold_str)

        out_name = self.lineEdit_probability_output_map.text().strip()
        if not out_name:
            out_name = suggested_name
        out_name = self._unique_layer_name(out_name)

        # --- Write the result to an in-memory GeoTIFF (GDAL /vsimem/) -----------
        # /vsimem/ keeps the raster entirely in RAM; nothing is written to disk,
        # while still letting QgsRasterLayer open it like a normal GDAL source.
        vsimem_path = "/vsimem/%s_%s.tif" % (out_name, id(self))
        driver = gdal.GetDriverByName("GTiff")
        ds_out = driver.Create(vsimem_path, ds_est.RasterXSize, ds_est.RasterYSize,
                               1, gdal.GDT_Float64)
        ds_out.SetGeoTransform(ds_est.GetGeoTransform())
        ds_out.SetProjection(ds_est.GetProjection())
        band_out = ds_out.GetRasterBand(1)
        band_out.SetNoDataValue(out_nodata)
        band_out.WriteArray(prob)
        band_out.FlushCache()
        ds_out = None
        ds_est = None
        ds_var = None

        result_layer = QgsRasterLayer(vsimem_path, out_name, "gdal")
        if not result_layer.isValid():
            QtWidgets.QMessageBox.critical(self, "Error",
                "Failed to create the in-memory probability raster layer.")
            return

        QgsProject.instance().addMapLayer(result_layer)
        self._populate_raster_combo()
        self._populate_probability_raster_combos()

        direction_txt = "exceeding" if higher else "not exceeding"
        QtWidgets.QMessageBox.information(
            self, "Done",
            "Probability map complete.\n"
            "New in-memory raster '%s' added to the project.\n"
            "Probability of %s threshold = %g." % (out_name, direction_txt, threshold)
        )

