    # Slot: run normal score transform
    # ------------------------------------------------------------------

    def run_ns_transform(self):
        """Compute NS transform and create a new memory layer with:
          - x, y  (coordinates from each feature's geometry centroid)
          - the original selected field
          - the normal-score column

        Unlike a typical "transformed copy" of the source layer, the output
        rows are sorted by ascending original value. This means the layer
        IS the GSLIB-style transformation table: column field_name holds the
        sorted original values, and column desired_ns_name holds the
        matching normal scores, row by row, with x/y kept aligned to the
        same point. No separate JSON/table column is needed, and
        run_back_transform() reads table_orig/table_ns directly from these
        two columns.
        """
        layer = self.mMapLayerComboBox.currentLayer()
        if layer is None:
            QtWidgets.QMessageBox.warning(self, "No Layer", "Please select a vector layer.")
            return

        field_name = self.mFieldComboBox.currentField()
        if not field_name:
            QtWidgets.QMessageBox.warning(self, "No Field", "Please select a field.")
            return

        # Output field names (no truncation needed for memory layers)
        desired_ns_name  = self.lineEdit_var_name_nscore_transform.text().strip() or (field_name + "_NSC_TRSF")

        # Jump immediately to 10% so the user sees instant feedback that the
        # computation has started, instead of staying at 0% during the
        # potentially slow feature-reading loop below (especially with
        # CSV-backed layers, where iterating features can take a moment
        # before the first progress update would otherwise appear).
        self.progressBar_nscore_transform.setValue(10)
        QtWidgets.QApplication.processEvents()

        # Check scipy
        try:
            from scipy.stats import norm as _scipy_norm  # noqa: F401
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "Dependency Error",
                                           "scipy is required for NS transform.")
            return

        # --- Collect data from source layer ---
        n_source = layer.featureCount()
        xs, ys, raw_vals = [], [], []
        for i, feat in enumerate(layer.getFeatures()):
            geom = feat.geometry()
            if geom and not geom.isNull():
                pt = geom.centroid().asPoint()
                xs.append(pt.x())
                ys.append(pt.y())
            else:
                xs.append(np.nan)
                ys.append(np.nan)
            v = feat[field_name]
            try:
                raw_vals.append(float(v))
            except (TypeError, ValueError):
                raw_vals.append(np.nan)
            if n_source > 0 and i % 50 == 0:
                self.progressBar_nscore_transform.setValue(10 + int((i + 1) / n_source * 40))
                QtWidgets.QApplication.processEvents()

        xs_arr  = np.array(xs)
        ys_arr  = np.array(ys)
        vals_arr = np.array(raw_vals)

        if np.all(np.isnan(xs_arr)):
            QtWidgets.QMessageBox.critical(
                self, "No Geometry Found",
                "None of the features in the source layer have a valid "
                "geometry, so no coordinates could be extracted.\n\n"
                "If this layer comes from a CSV file, check that the X/Y "
                "columns were correctly recognised when the file was loaded "
                "(this commonly fails if the CSV starts with a UTF-8 BOM, "
                "which can prevent QGIS from matching the 'x'/'y' column "
                "names). Try re-saving the CSV without a BOM, or set the "
                "X/Y field names explicitly when adding the delimited text layer."
            )
            return

        # Only rows with both a valid value AND a valid geometry can be part
        # of the transformation table (need x, y to plot/use them, and need
        # the value to compute the normal score).
        valid_mask = ~np.isnan(vals_arr) & ~np.isnan(xs_arr) & ~np.isnan(ys_arr)
        valid_vals = vals_arr[valid_mask]
        valid_xs   = xs_arr[valid_mask]
        valid_ys   = ys_arr[valid_mask]

        if len(valid_vals) < 2:
            QtWidgets.QMessageBox.critical(self, "Error",
                                           "Not enough valid values to compute NS transform.")
            return

        # --- Compute NS transform ---
        # sort_idx reorders valid_vals ascending; table_orig == valid_vals[sort_idx]
        # and table_ns are exactly that sorted sequence and its normal scores.
        # Applying the same sort_idx to valid_xs/valid_ys keeps every row
        # pointing at the correct original point.
        _, table_orig, table_ns, sort_idx = self._nscore(valid_vals)
        sorted_xs = valid_xs[sort_idx]
        sorted_ys = valid_ys[sort_idx]
        self.progressBar_nscore_transform.setValue(50)
        QtWidgets.QApplication.processEvents()

        # --- Unique output layer name ---
        base_lyr_name  = layer.name() + "_NSC_TRSF"
        existing_names = {l.name() for l in QgsProject.instance().mapLayers().values()}
        out_lyr_name   = base_lyr_name
        suffix = 1
        while out_lyr_name in existing_names:
            out_lyr_name = "%s_%d" % (base_lyr_name, suffix)
            suffix += 1

        # --- Create memory layer (Point, same CRS) ---
        # Fall back to a default CRS if the source layer has none (this can
        # happen with CSV layers whose header was not parsed correctly, e.g.
        # a UTF-8 BOM corrupting the first column name so QGIS could not
        # auto-recognise the X/Y columns and never assigned a CRS). Without
        # this fallback, "Point?crs=" is an invalid URI and the new layer
        # silently fails to be created.
        crs = layer.crs()
        if crs is None or not crs.isValid():
            crs_str = "EPSG:4326"
            QtWidgets.QMessageBox.warning(
                self, "No CRS Found",
                "The source layer has no valid coordinate reference system "
                "(this can happen with CSV files that were not recognised "
                "correctly, for example due to encoding issues in the header).\n\n"
                "The new layer will be created with a default CRS "
                "(EPSG:4326). You may need to reproject it manually."
            )
        else:
            crs_str = crs.authid() or crs.toWkt()
            if not crs_str:
                crs_str = "EPSG:4326"
        out_layer = QgsVectorLayer("Point?crs=%s" % crs_str, out_lyr_name, "memory")
        if not out_layer.isValid():
            QtWidgets.QMessageBox.critical(
                self, "Error",
                "Failed to create the output memory layer (invalid CRS or "
                "geometry definition)."
            )
            return
        pr = out_layer.dataProvider()

        # Define exactly the columns we want:
        #   x, y  (Double) - coordinates of the point that has this value
        #   <field_name>   (Double) - original value, SORTED ascending
        #   <ns_name>      (Double) - corresponding normal score
        pr.addAttributes([
            QgsField("x",              QVariant.Double),
            QgsField("y",              QVariant.Double),
            QgsField(field_name,       QVariant.Double),
            QgsField(desired_ns_name,  QVariant.Double),
        ])
        out_layer.updateFields()

        out_fields = out_layer.fields()
        idx_x     = out_fields.indexOf("x")
        idx_y     = out_fields.indexOf("y")
        idx_orig  = out_fields.indexOf(field_name)
        idx_ns    = out_fields.indexOf(desired_ns_name)

        # --- Build features, one per row of the (sorted) transformation table ---
        new_features = []
        n_out = len(table_orig)
        for i in range(n_out):
            feat = QgsFeature(out_fields)
            feat.setGeometry(QgsGeometry.fromPointXY(QgsPointXY(float(sorted_xs[i]), float(sorted_ys[i]))))
            feat.setAttribute(idx_x,    float(sorted_xs[i]))
            feat.setAttribute(idx_y,    float(sorted_ys[i]))
            feat.setAttribute(idx_orig, float(table_orig[i]))
            feat.setAttribute(idx_ns,   float(table_ns[i]))
            new_features.append(feat)
            if n_out > 0 and i % 50 == 0:
                self.progressBar_nscore_transform.setValue(50 + int((i + 1) / n_out * 45))
                QtWidgets.QApplication.processEvents()

        ok, _ = pr.addFeatures(new_features)
        if not ok:
            QtWidgets.QMessageBox.critical(self, "Error",
                                           "Failed to add features to the output layer.")
            return

        out_layer.updateExtents()
        QgsProject.instance().addMapLayer(out_layer)
        self.progressBar_nscore_transform.setValue(100)

        # Refresh combos
        self._populate_nscore_layer_combo(select_layer=out_layer)
        self._populate_back_transform_variable_combo()
        self.mMapLayerComboBox.setLayer(out_layer)
        self.mFieldComboBox.setLayer(out_layer)
        self.mFieldComboBox.setField(desired_ns_name)

        QtWidgets.QMessageBox.information(
            self, "Done",
            "Normal Score Transform complete.\n"
            "New layer '%s' added to the project (%d rows, sorted by "
            "ascending value).\n"
            "  x, y : coordinates\n"
            "  %s   : original values (sorted ascending)\n"
            "  %s   : normal scores\n\n"
            "This layer itself is the GSLIB-style transformation table: "
            "no separate table column is stored." % (
                out_lyr_name, len(table_orig), field_name, desired_ns_name)
        )

