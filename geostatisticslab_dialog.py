    @staticmethod
    def _cell_decluster_weights(coords, values, cell_size, n_offsets=4, rng=None):
        """Compute GSLIB-style cell declustering weights for one cell size.

        The study area is covered by a grid of square cells of the given
        size; each point's raw weight is 1/(number of occupied-cell points
        in its cell). To avoid the result depending on an arbitrary grid
        origin, the grid is shifted by several random offsets and the
        weights are averaged across offsets. Weights are then normalised so
        their mean is 1 (i.e. they sum to the number of points), matching
        the standard GSLIB convention.

        Returns (weights, weighted_mean).
        """
        if rng is None:
            rng = np.random.default_rng(seed=12345)

        n = len(values)
        if n == 0 or cell_size <= 0:
            return np.ones(n), float(np.mean(values)) if n > 0 else np.nan

        x_min, y_min = coords[:, 0].min(), coords[:, 1].min()
        accum_weights = np.zeros(n)

        for _ in range(n_offsets):
            off_x = rng.uniform(0, cell_size)
            off_y = rng.uniform(0, cell_size)
            cell_ix = np.floor((coords[:, 0] - x_min + off_x) / cell_size).astype(np.int64)
            cell_iy = np.floor((coords[:, 1] - y_min + off_y) / cell_size).astype(np.int64)
            cell_id = cell_ix.astype(np.int64) * 1_000_003 + cell_iy.astype(np.int64)

            # Count points per occupied cell, then map back to each point
            unique_cells, inverse, counts = np.unique(cell_id, return_inverse=True, return_counts=True)
            points_per_cell = counts[inverse]
            accum_weights += 1.0 / points_per_cell

        raw_weights = accum_weights / n_offsets
        # Normalise so the mean weight is 1 (GSLIB convention)
        norm_weights = raw_weights * (n / np.sum(raw_weights))

        weighted_mean = float(np.sum(norm_weights * values) / np.sum(norm_weights))
        return norm_weights, weighted_mean

    def run_declustering(self):
        """Run cell declustering: test a range of cell sizes (for the
        diagnostic plot, weighted mean vs cell size), then compute the final
        per-point weights for doubleSpinBox_cell_size and create a NEW
        in-memory layer with all the original columns plus a
        'decluster_weight' field.

        A new layer is created (rather than editing the active layer in
        place) so this works even on read-only sources (CSV, etc.) and never
        modifies the user's original data.
        """
        layer = self.mMapLayerComboBox.currentLayer()
        field_name = self.mFieldComboBox.currentField()
        if not layer or not field_name:
            QtWidgets.QMessageBox.warning(self, "Missing Input",
                "Please select a layer and a field.")
            return

        # Jump immediately to 10% so the user sees instant feedback that the
        # computation has started, instead of staying at 0% while the
        # feature-collection loop below runs (can be slow on CSV-backed
        # layers before the first progress update would otherwise appear).
        self.progressBar_decluster.setValue(10)
        QtWidgets.QApplication.processEvents()

        # --- Collect ALL features (not just the ones with a valid value),
        # so every original row is preserved in the output layer. ---------
        all_feats = list(layer.getFeatures())
        if len(all_feats) == 0:
            QtWidgets.QMessageBox.critical(self, "Error", "The layer has no features.")
            return

        coords_all = []
        values_all = []
        valid_mask = []
        for f in all_feats:
            geom = f.geometry()
            if geom and not geom.isNull():
                pt = geom.asPoint()
                coords_all.append([pt.x(), pt.y()])
            else:
                coords_all.append([np.nan, np.nan])

            val = f[field_name]
            try:
                if val != NULL and val is not None:
                    values_all.append(float(val))
                    valid_mask.append(True)
                else:
                    values_all.append(np.nan)
                    valid_mask.append(False)
            except (TypeError, ValueError):
                values_all.append(np.nan)
                valid_mask.append(False)

        coords_all = np.array(coords_all)
        values_all = np.array(values_all)
        valid_mask = np.array(valid_mask) & np.isfinite(coords_all[:, 0]) & np.isfinite(coords_all[:, 1])

        coords = coords_all[valid_mask]
        values = values_all[valid_mask]
        n = len(values)
        if n < 2:
            QtWidgets.QMessageBox.critical(self, "Error",
                "Not enough valid points (with both a value and a geometry) "
                "to run declustering.")
            return

        naive_mean = float(np.mean(values))

        # --- Diagnostic curve: weighted mean vs cell size -----------------------
        cs_min  = self.doubleSpinBox_cell_size_min.value()
        cs_max  = self.doubleSpinBox_cell_size_max.value()
        n_steps = int(self.spinBox_cell_size_steps.value())

        if cs_min > 0 and cs_max > cs_min and n_steps > 1:
            cell_sizes = np.linspace(cs_min, cs_max, n_steps)
            weighted_means = np.empty(n_steps)
            rng = np.random.default_rng(seed=12345)
            for i, cs in enumerate(cell_sizes):
                _, wmean = self._cell_decluster_weights(coords, values, cs, rng=rng)
                weighted_means[i] = wmean
                self.progressBar_decluster.setValue(10 + int((i + 1) / n_steps * 40))
                if i % 2 == 0:
                    QtWidgets.QApplication.processEvents()

            self.figure_decluster.clear()
            ax = self.figure_decluster.add_subplot(111)
            ax.plot(cell_sizes, weighted_means, marker='o', markersize=4,
                   color='#1f77b4', linewidth=1.3, label='Declustered mean')
            ax.axhline(naive_mean, color='red', linewidth=1.2, linestyle='--',
                      label='Naive mean (unweighted)')
            ax.set_xlabel('Cell size', fontsize=8)
            ax.set_ylabel('Weighted mean of %s' % field_name, fontsize=8)
            ax.set_title('Cell Declustering Diagnostic', fontsize=10)
            ax.grid(True, linestyle=':', linewidth=0.4, alpha=0.5)
            ax.tick_params(axis='both', labelsize=7)
            ax.legend(fontsize=7)
            self.figure_decluster.tight_layout()
            self.canvas_decluster.draw()

        # --- Final weights for the user-chosen cell size -------------------------
        final_cell_size = self.doubleSpinBox_cell_size.value()
        if final_cell_size <= 0:
            QtWidgets.QMessageBox.warning(self, "Invalid Cell Size",
                "Please set a positive cell size to compute the final weights.")
            return

        weights_valid, weighted_mean = self._cell_decluster_weights(coords, values, final_cell_size)
        self.progressBar_decluster.setValue(60)
        QtWidgets.QApplication.processEvents()

        # Map weights back to the full feature list (NULL for features that
        # were excluded, e.g. missing value or missing geometry).
        weights_full = np.full(len(all_feats), np.nan)
        weights_full[valid_mask] = weights_valid

        # --- Unique output layer name ---------------------------------------------
        base_lyr_name  = layer.name() + "_decluster"
        existing_names = {l.name() for l in QgsProject.instance().mapLayers().values()}
        out_lyr_name   = base_lyr_name
        suffix = 1
        while out_lyr_name in existing_names:
            out_lyr_name = "%s_%d" % (base_lyr_name, suffix)
            suffix += 1

        # --- Create memory layer (same geometry type and CRS as the source) -----
        geom_type_map = {
            0: "Point", 1: "LineString", 2: "Polygon",
            3: "MultiPoint", 4: "MultiLineString", 5: "MultiPolygon"
        }
        geom_type = geom_type_map.get(layer.geometryType(), "Point")
        crs = layer.crs()
        crs_str = (crs.authid() or crs.toWkt()) if (crs and crs.isValid()) else "EPSG:4326"
        out_layer = QgsVectorLayer("%s?crs=%s" % (geom_type, crs_str), out_lyr_name, "memory")
        if not out_layer.isValid():
            QtWidgets.QMessageBox.critical(self, "Error",
                "Failed to create the output memory layer.")
            return

        pr = out_layer.dataProvider()

        # Copy all original fields, then add the weight field
        original_fields = list(layer.fields())
        field_out_name = "decluster_weight"
        # Avoid a name collision with an existing column of the same name
        if field_out_name in {f.name() for f in original_fields}:
            i = 1
            while ("%s_%d" % (field_out_name, i)) in {f.name() for f in original_fields}:
                i += 1
            field_out_name = "%s_%d" % (field_out_name, i)

        pr.addAttributes(original_fields + [QgsField(field_out_name, QVariant.Double)])
        out_layer.updateFields()
        out_fields = out_layer.fields()
        weight_idx = out_fields.indexOf(field_out_name)

        # --- Build and add features, copying every original attribute -----------
        new_features = []
        n_feats = len(all_feats)
        for i, src_feat in enumerate(all_feats):
            new_feat = QgsFeature(out_fields)
            new_feat.setGeometry(src_feat.geometry())
            for j, field in enumerate(original_fields):
                new_feat.setAttribute(j, src_feat[field.name()])
            w = weights_full[i]
            new_feat.setAttribute(weight_idx, None if np.isnan(w) else float(w))
            new_features.append(new_feat)
