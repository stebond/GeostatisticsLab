    def add_model_structure_row(self, model_type="Spherical", default_sill=0.0):
        """Programmatically appends a highly interactive custom row to the modeling table matrix."""
        row = self.tableWidget_models.rowCount()
        self.tableWidget_models.insertRow(row)
        
        combo = QtWidgets.QComboBox()
        combo.addItems(["Nugget", "Spherical", "Exponential", "Gaussian", "Linear", "Cubic"])
        combo.setCurrentText(model_type)
        combo.currentIndexChanged.connect(self.on_plot_options_changed)
        self.tableWidget_models.setCellWidget(row, 0, combo)
        
        def create_spinbox(val, max_val=1e6, decimals=4):
            sb = QtWidgets.QDoubleSpinBox()
            sb.setDecimals(decimals)
            sb.setMinimum(-360.0 if max_val == 360 else 0.0)
            sb.setMaximum(max_val)
            sb.setValue(val)
            sb.setSingleStep(1.0 if max_val == 360 else 10.0)
            sb.valueChanged.connect(self.on_plot_options_changed)
            return sb

        self.tableWidget_models.setCellWidget(row, 1, create_spinbox(default_sill, max_val=1e9)) 
        self.tableWidget_models.setCellWidget(row, 2, create_spinbox(100.0))                     
        self.tableWidget_models.setCellWidget(row, 3, create_spinbox(100.0))                     
        self.tableWidget_models.setCellWidget(row, 4, create_spinbox(0.0))                       
        self.tableWidget_models.setCellWidget(row, 5, create_spinbox(0.0, max_val=360))          
        self.tableWidget_models.setCellWidget(row, 6, create_spinbox(0.0, max_val=360))          
        self.tableWidget_models.setCellWidget(row, 7, create_spinbox(0.0, max_val=360))          

    def remove_selected_model_row(self):
        if self.tableWidget_models.rowCount() <= 1:
            QtWidgets.QMessageBox.warning(
                self, "Warning", 
                "Cannot delete. The model must contain at least one structure."
            )
            return

        current_row = self.tableWidget_models.currentRow()
        if current_row < 0:
            QtWidgets.QMessageBox.warning(
                self, "Missing Selection", 
                "Select at least one structure from the table by clicking on the row."
            )
            return

        self.tableWidget_models.removeRow(current_row)
        self.on_plot_options_changed()

    def update_fit_error(self):
        if self.last_lags is None or self.last_results is None:
            self.label_11_fit_error.setText("Fit Error (SSE): N/A")
            return

        structures = self.get_models_from_table()
        lags = self.last_lags
        results = self.last_results

        sse = 0.0
        has_valid_pairs = False

        for az, (gamma, counts, _, h_eff, _pd) in results.items():
            for k in range(len(lags)):
                if not np.isnan(gamma[k]) and counts[k] > 0:
                    h_for_fit = h_eff[k]
                    g_theo = self.calculate_theoretical_semivariance(np.array([h_for_fit]), az, structures)[0]
                    sse += counts[k] * ((gamma[k] - g_theo) ** 2)
                    has_valid_pairs = True

        if has_valid_pairs:
            self.label_11_fit_error.setText(f"Fit Error (SSE): {sse:.4f}")
        else:
            self.label_11_fit_error.setText("Fit Error (SSE): N/A")

    def _setup_var_fit_setting_table(self):
        """Populate tableWidget_var_fit_setting with one row per variogram model.

        Column 0 - model name (read-only)
        Column 1 - checkbox (checked = include in autofit)
        All models are enabled by default.
        """
        tw = self.tableWidget_var_fit_setting
        models = self._AUTOFIT_MODELS

        tw.setRowCount(len(models))
        tw.setColumnCount(2)
        tw.setHorizontalHeaderLabels(["Model", "Use in autofit"])
        tw.verticalHeader().setVisible(False)
        tw.horizontalHeader().setStretchLastSection(True)
        tw.setEditTriggers(QtWidgets.QAbstractItemView.NoEditTriggers)
        tw.setSelectionMode(QtWidgets.QAbstractItemView.NoSelection)

        for row, name in enumerate(models):
            # Model name - centred, not editable
            name_item = QtGui.QStandardItem(name)
            item = QtWidgets.QTableWidgetItem(name)
            item.setFlags(QtCore.Qt.ItemIsEnabled)
            item.setTextAlignment(QtCore.Qt.AlignCenter)
            tw.setItem(row, 0, item)

            # Checkbox widget centred in cell
            chk = QtWidgets.QCheckBox()
            chk.setChecked(True)
            container = QtWidgets.QWidget()
            layout = QtWidgets.QHBoxLayout(container)
            layout.addWidget(chk)
            layout.setAlignment(QtCore.Qt.AlignCenter)
            layout.setContentsMargins(0, 0, 0, 0)
            tw.setCellWidget(row, 1, container)

        tw.resizeColumnsToContents()

    def _get_enabled_autofit_models(self):
        """Return the list of model names whose checkbox is currently checked."""
        tw = self.tableWidget_var_fit_setting
        enabled = []
        for row in range(tw.rowCount()):
            container = tw.cellWidget(row, 1)
            if container is not None:
                chk = container.findChild(QtWidgets.QCheckBox)
                if chk is not None and chk.isChecked():
                    item = tw.item(row, 0)
                    if item is not None:
                        enabled.append(item.text())
        return enabled

    def run_autofit(self):
        self.progressBar_autofit.setValue(0)
        current_layer = self.mMapLayerComboBox.currentLayer()
        current_field = self.mFieldComboBox.currentField()

        if not current_layer or not current_field:
            QtWidgets.QMessageBox.warning(self, "Warning", "Please select a valid layer and field.")
            return

        has_cache = (self.last_lags is not None and self.last_results is not None)
        cache_is_stale = (self.last_layer_id != current_layer.id() or self.last_field_name != current_field)

        if has_cache and cache_is_stale:
            reply = QtWidgets.QMessageBox.question(
                self, "Variable Changed",
                "The selected field or layer has changed. Recalculate variogram before autofit?",
                QtWidgets.QMessageBox.Yes | QtWidgets.QMessageBox.No,
                QtWidgets.QMessageBox.Yes
            )
            if reply == QtWidgets.QMessageBox.Yes:
                self.calculate_variogram()
                if self.last_field_name != current_field or self.last_layer_id != current_layer.id():
                    return
            else:
                return

        if self.last_lags is None or self.last_results is None:
            QtWidgets.QMessageBox.warning(self, "Missing Data", "Calculate the experimental variogram first!")
            return

        layer_values = []
        for f in current_layer.getFeatures():
            val = f[current_field]
            if val != NULL and val is not None:
                try:
                    layer_values.append(float(val))
                except (ValueError, TypeError):
                    continue

        if len(layer_values) < 2:
            return

        sample_variance = np.var(layer_values)

        try:
            from scipy.optimize import minimize
        except ImportError:
            QtWidgets.QMessageBox.critical(self, "Dependency Error", "The 'scipy' library is required for autofit.")
            return

        n_struct = self.spinBox_nstruct_autofit.value()
        lags = self.last_lags
        results = self.last_results
        
        all_h, all_az, all_gamma, all_weights = [], [], [], []
        for az, (gamma, counts, _, h_eff, _pd) in results.items():
            for k in range(len(lags)):
                if not np.isnan(gamma[k]) and counts[k] > 0:
                    all_h.append(h_eff[k])
                    all_az.append(az)
                    all_gamma.append(gamma[k])
                    all_weights.append(counts[k])
                    
        if len(all_gamma) == 0:
            return

        all_h, all_az, all_gamma, all_weights = np.array(all_h), np.array(all_az), np.array(all_gamma), np.array(all_weights)
        max_h = np.max(all_h)

        if max_h <= 0 or sample_variance <= 0:
            QtWidgets.QMessageBox.warning(
                self, "Cannot Autofit",
                "The experimental variogram has no usable spread (max distance "
                "or sample variance is zero). Check the input data and the "
                "lag/distance settings."
            )
            return

        # -- Read which models the user has enabled in tableWidget_var_fit_setting
        enabled_models = self._get_enabled_autofit_models()
        if not enabled_models:
            QtWidgets.QMessageBox.warning(
                self, "No Models Selected",
                "Please enable at least one variogram model in the 'Autofit Settings' table."
            )
            return

        # Spatial models are all enabled models except Nugget
        spatial_models = [m for m in enabled_models if m != "Nugget"]
        has_nugget = "Nugget" in enabled_models

        candidates_set = []
        if n_struct == 1:
            # One candidate per enabled model
            for m in enabled_models:
                candidates_set.append([m])
        else:
            # Multi-structure combinations using only enabled models
            for m in spatial_models:
                if has_nugget:
                    candidates_set.append(["Nugget"] + [m] * (n_struct - 1))
                candidates_set.append([m] * n_struct)
            # Fallback: if only Nugget is enabled, allow single-nugget candidate
            if not spatial_models and has_nugget:
                candidates_set.append(["Nugget"])

        if not candidates_set:
            QtWidgets.QMessageBox.warning(
                self, "No Valid Combinations",
                "The selected models cannot form a valid candidate set for the "
                f"requested number of structures ({n_struct}).\n"
                "Try enabling more models or reducing the structure count."
            )
            return

        best_sse = float('inf')
        best_params, best_types = None, None

        n_candidates = len(candidates_set)
        self.progressBar_autofit.setMinimum(0)
        self.progressBar_autofit.setMaximum(100)
        self.progressBar_autofit.setValue(0)
        QtWidgets.QApplication.processEvents()

        # Lower bound for ranges must scale with the data, not be a fixed
        # constant: a fixed 1e-3 floor can exceed max_h*0.3 when the study
        # area is very small (e.g. coordinates in km, or a compact dataset),
        # which made x0 fall below its own bound and crashed L-BFGS-B.
        range_floor = max(max_h * 1e-4, 1e-12)

        # Small inward margin so x0 never sits exactly ON a bound. Some
        # scipy/Windows builds evaluate the numerical gradient with a
        # symmetric step (x0 +/- eps); if x0 == bound, the "minus" side
        # of that step can fall outside the bound and raise
        # "x0 violates bound constraints" even though x0 itself is valid.
        AZIMUTH_EPS = 1e-6  # degrees, well inside (0, 180)

        # -- Multi-start over initial azimuth -----------------------------------
        # L-BFGS-B is a local, gradient-based optimizer: starting every fit
        # from azimuth ~0 deg means it only ever finds the nearest local
        # minimum in azimuth, which is often NOT the true best-fitting
        # anisotropy direction (the SSE surface vs. azimuth typically has
        # multiple local minima, especially given the natural 90/180-degree
        # symmetries of anisotropic models). Trying several evenly-spaced
        # starting angles and keeping the best result across all of them is
        # the standard fix for this kind of optimizer getting stuck.
        azimuth_starts = [AZIMUTH_EPS, 30.0, 60.0, 90.0, 120.0, 150.0]
        n_angle_starts = len(azimuth_starts)
        n_total_steps = n_candidates * n_angle_starts

        for candidate_num, types in enumerate(candidates_set):
            spatial_count = sum(1 for t in types if t != "Nugget")
            has_nugget = "Nugget" in types

            def objective(params, types=types):
                structs = []
                idx = 0
                for comp_type in types:
                    if comp_type == "Nugget":
                        structs.append({'type': 'Nugget', 'sill': params[idx], 'r1': 0, 'r2': 0, 'r3': 0, 'a1': 0, 'a2': 0, 'a3': 0})
                        idx += 1
                    else:
                        sill, r1, r2, a1 = params[idx:idx+4]
                        structs.append({'type': comp_type, 'sill': sill, 'r1': r1, 'r2': r2, 'r3': 0, 'a1': a1, 'a2': 0, 'a3': 0})
                        idx += 4
                sse = 0.0
                for h, az, g_exp, w in zip(all_h, all_az, all_gamma, all_weights):
                    g_theo = self.calculate_theoretical_semivariance(np.array([h]), az, structs)[0]
                    sse += w * ((g_exp - g_theo) ** 2)
                return sse

            # Try several starting azimuths for this model combination, and
            # keep whichever converges to the lowest SSE. A single starting
            # angle (e.g. ~0 deg) can leave L-BFGS-B stuck in a local
            # minimum, which is exactly the "manually nudging the angle
            # gives a much lower error" symptom this addresses.
            candidate_best_sse = float('inf')
            candidate_best_params = None

            for angle_start_num, az_start in enumerate(azimuth_starts):
                x0, bounds = [], []
                for t in types:
                    if t == "Nugget":
                        x0.append(sample_variance * 0.15 if spatial_count > 0 else sample_variance)
                        bounds.append((0.0, sample_variance * 2.0))
                    else:
                        x0.extend([(sample_variance * 0.85) / spatial_count if has_nugget else sample_variance / spatial_count, max_h * 0.5, max_h * 0.3, az_start])
                        bounds.extend([(0.0, sample_variance * 2.0), (range_floor, max_h * 2.0), (range_floor, max_h * 2.0), (0.0, 180.0)])

                # Defensive clamp: guarantee x0 always lies strictly within
                # its own bounds (not just <=/>=, but with a tiny inward
                # margin), regardless of how sample_variance / max_h end up
                # being valued.
                safe_x0 = []
                safe_bounds = []
                for v, (lo, hi) in zip(x0, bounds):
                    if not np.isfinite(v):
                        v = lo if np.isfinite(lo) else 0.0
                    if not np.isfinite(lo):
                        lo = -1e12
                    if not np.isfinite(hi):
                        hi = 1e12
                    if hi < lo:
                        lo, hi = hi, lo
                    span = hi - lo
                    margin = span * 1e-6 if span > 0 else 0.0
                    v = min(max(v, lo + margin), hi - margin) if span > 0 else lo
                    safe_x0.append(v)
                    safe_bounds.append((lo, hi))
                x0, bounds = safe_x0, safe_bounds

                step_num = candidate_num * n_angle_starts + angle_start_num
                try:
                    res = minimize(objective, x0, method='L-BFGS-B', bounds=bounds)
                except ValueError:
                    # Skip this starting angle rather than aborting the
                    # whole candidate; other starting angles can still be
                    # evaluated.
                    self.progressBar_autofit.setValue(int((step_num + 1) / n_total_steps * 100))
                    QtWidgets.QApplication.processEvents()
                    continue

                if res.success and res.fun < candidate_best_sse:
                    candidate_best_sse = res.fun
                    candidate_best_params = res.x

                self.progressBar_autofit.setValue(int((step_num + 1) / n_total_steps * 100))
                QtWidgets.QApplication.processEvents()

            if candidate_best_params is not None and candidate_best_sse < best_sse:
                best_sse = candidate_best_sse
                best_params = candidate_best_params
                best_types = types

        self.progressBar_autofit.setValue(100)

        if best_params is not None:
            self.tableWidget_models.blockSignals(True)
            self.tableWidget_models.setRowCount(0)
            idx = 0
            for comp_type in best_types:
                row = self.tableWidget_models.rowCount()
                self.tableWidget_models.insertRow(row)
                combo = QtWidgets.QComboBox()
                combo.addItems(["Nugget", "Spherical", "Exponential", "Gaussian", "Linear", "Cubic"])
                combo.setCurrentText(comp_type)
                combo.currentIndexChanged.connect(self.on_plot_options_changed)
                self.tableWidget_models.setCellWidget(row, 0, combo)
                
                def create_spinbox(val, max_val=1e6):
                    sb = QtWidgets.QDoubleSpinBox()
                    sb.setDecimals(4)
                    sb.setMinimum(-360.0 if max_val == 360 else 0.0)
                    sb.setMaximum(max_val)
                    sb.setValue(val)
                    sb.valueChanged.connect(self.on_plot_options_changed)
                    return sb

                if comp_type == "Nugget":
                    self.tableWidget_models.setCellWidget(row, 1, create_spinbox(best_params[idx], 1e9))
                    for col in range(2, 5): self.tableWidget_models.setCellWidget(row, col, create_spinbox(0.0))
                    for col in range(5, 8): self.tableWidget_models.setCellWidget(row, col, create_spinbox(0.0, 360))
                    idx += 1
                else:
                    sill, r1, r2, a1 = best_params[idx:idx+4]
                    self.tableWidget_models.setCellWidget(row, 1, create_spinbox(sill, 1e9))
                    self.tableWidget_models.setCellWidget(row, 2, create_spinbox(r1))
                    self.tableWidget_models.setCellWidget(row, 3, create_spinbox(r2))
                    self.tableWidget_models.setCellWidget(row, 4, create_spinbox(0.0))
                    self.tableWidget_models.setCellWidget(row, 5, create_spinbox(a1, 360))
                    self.tableWidget_models.setCellWidget(row, 6, create_spinbox(0.0, 360))
                    self.tableWidget_models.setCellWidget(row, 7, create_spinbox(0.0, 360))
                    idx += 4

            self.tableWidget_models.blockSignals(False)
            self.variogram_computed = True # Mark as correctly computed/fitted
            self.on_plot_options_changed()
            if not self.checkBox_theoretical_model.isChecked():
                    self.checkBox_theoretical_model.setChecked(True)
        else:
            QtWidgets.QMessageBox.warning(
                self, "Autofit Failed",
                "None of the candidate model combinations could be fitted.\n"
                "Try enabling more models, changing the number of structures, "
                "or checking the experimental variogram data."
            )

